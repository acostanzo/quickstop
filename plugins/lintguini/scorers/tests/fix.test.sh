# -- 4. Configured + unsafe-fixable + --apply-semantic ------------------

test_python_apply_semantic() {
  local repo; repo="$(mktmp_repo py-semantic)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_safe_diff "$mb"
  # Pre-snapshot of working tree so we can verify it's untouched after.
  mkdir -p "$repo/src"
  echo 'def existing(): pass' > "$repo/src/seed.py"
  local pre_hash; pre_hash="$(find "$repo" -type f -exec sha256sum {} + | LC_ALL=C sort | sha256sum)"

  run_fix_split "$repo" "$mb" --apply-semantic
  local rc=$?
  # The semantic mock emits the dead-code-removal diff (different from
  # the safe `import os, sys` split).
  if [[ "$STDOUT" == *"def dead"* && "$STDOUT" == *"--- a/src/x.py"* ]]; then note_pass
  else note_fail "py semantic: missing unsafe-mode diff. stdout: $STDOUT"; fi
  # Working tree clean afterward — semantic must not write.
  local post_hash; post_hash="$(find "$repo" -type f -exec sha256sum {} + | LC_ALL=C sort | sha256sum)"
  if [[ "$pre_hash" == "$post_hash" ]]; then note_pass
  else note_fail "py semantic: working tree changed after --apply-semantic"; fi
  if (( rc == 1 )); then note_pass
  else note_fail "py semantic: expected exit 1, got $rc"; fi
}

