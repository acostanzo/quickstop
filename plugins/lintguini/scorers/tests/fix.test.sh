# -- 10. Copy-and-diff fallback — biome (no native diff mode) -----------

test_copy_and_diff_biome() {
  local repo; repo="$(mktmp_repo copy-diff)"
  local mb; mb="$(mktmp_mockbin)"
  write_javascript_config "$repo"
  mkdir -p "$repo/src"
  printf 'var x = true;\n' > "$repo/src/render.js"
  mock_biome_writes "$mb"

  local pre_content; pre_content="$(cat "$repo/src/render.js")"

  run_fix_split "$repo" "$mb"
  local rc=$?

  # The script must snapshot, run biome in the copy (mock writes to
  # the copy), then diff. Original must be untouched.
  local post_content; post_content="$(cat "$repo/src/render.js")"
  if [[ "$pre_content" == "$post_content" ]]; then note_pass
  else note_fail "copy-and-diff: original src/render.js was modified (working tree mutation)"; fi

  # Output must be a unified diff with a/ b/ headers.
  if [[ "$STDOUT" == *"--- a/src/render.js"* && "$STDOUT" == *"+++ b/src/render.js"* ]]; then
    note_pass
  else
    note_fail "copy-and-diff: missing a/ b/ headers. stdout: $STDOUT"
  fi
  # The diff should reflect the mock's edit (var → const safe_fix).
  if [[ "$STDOUT" == *"-var x = true;"* && "$STDOUT" == *"+const safe_fix = true;"* ]]; then
    note_pass
  else
    note_fail "copy-and-diff: diff content unexpected. stdout: $STDOUT"
  fi
  if (( rc == 1 )); then note_pass
  else note_fail "copy-and-diff: expected exit 1, got $rc"; fi
}

