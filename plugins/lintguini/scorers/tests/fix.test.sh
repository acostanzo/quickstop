#!/usr/bin/env bash
# fix.test.sh — exercise bin/lintguini-fix.sh end-to-end against mocked
# fix tools on PATH. The contract under test is the dispatch logic and
# output projection across read-only / --apply / --apply-semantic
# modes, NOT the fixers themselves. Mocks emit known native output;
# the script's job is to (a) call the right tool with the right flags
# per mode, (b) project output to the contract shape, (c) handle
# empty-scope and tool-missing.
#
# Real fix tools (ruff, biome, cargo, rubocop, standardrb, gofmt) are
# not installed on the test box. Each test stages a tempdir on PATH
# containing a fake binary that branches on argv to emit per-mode
# output, then verifies the script's projection.

set -uo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_ROOT="$(cd "$HERE/../.." && pwd)"
SCRIPT="$PLUGIN_ROOT/bin/lintguini-fix.sh"

PASS=0
FAIL=0
FAILURES=()

note_pass() { PASS=$((PASS + 1)); }
note_fail() {
  FAIL=$((FAIL + 1))
  FAILURES+=("$1")
}

if [[ ! -x "$SCRIPT" ]]; then
  echo "FATAL: $SCRIPT not executable" >&2
  exit 2
fi

PROVENANCE='Generated by lintguini — see plugins/pronto/references/roll-your-own/lint-posture.md for the source of truth.'

TMPROOT="$(mktemp -d -t lintguini-fix-tests.XXXXXX)"
trap 'rm -rf "$TMPROOT"' EXIT

mktmp_repo() {
  local d
  d="$(mktemp -d -p "$TMPROOT" "$1.XXXXXX")"
  printf '%s' "$d"
}

mktmp_mockbin() {
  local d
  d="$(mktemp -d -p "$TMPROOT" "mockbin.XXXXXX")"
  printf '%s' "$d"
}

# -- Provenance-marked config writers (mirror lint/format tests) --------

write_python_config() {
  cat > "$1/pyproject.toml" <<EOF
# $PROVENANCE
[tool.ruff]
line-length = 100

[tool.ruff.lint]
select = ["E", "F", "I"]
EOF
}

write_javascript_config() {
  cat > "$1/biome.json" <<EOF
{
  "_provenance": "$PROVENANCE",
  "_band": "strict",
  "\$schema": "https://biomejs.dev/schemas/latest/schema.json",
  "linter": { "enabled": true },
  "formatter": { "enabled": true, "indentWidth": 2 }
}
EOF
  echo '{"name":"demo"}' > "$1/package.json"
}

write_ruby_standardrb_config() {
  cat > "$1/standard.yml" <<EOF
# $PROVENANCE
ruby_version: 3.3
EOF
  cat > "$1/Gemfile" <<'EOF'
source "https://rubygems.org"
gem "standard"
EOF
}

# -- Mocks --------------------------------------------------------------
#
# Each mock branches on argv to handle the modes lintguini-fix dispatches:
#   - check --fix --diff       -> safe-fix preview (read-only mode)
#   - check --fix              -> safe-fix apply
#   - check --fix --unsafe-fixes --diff -> unsafe-fix preview (semantic)
#   - check --write [--unsafe] -> biome's apply / unsafe-apply
#
# Diff blobs use the contract's a/<path> / b/<path> shape so the
# script's normalize_diff is a no-op for these tests; the production
# normalization is exercised via ruff's bare-path output in test 1b.

mock_ruff_safe_diff() {
  cat > "$1/ruff" <<'MOCK'
#!/usr/bin/env bash
case "$*" in
  *check*--fix*--unsafe-fixes*--diff*)
    cat <<'DIFF'
--- a/src/x.py
+++ b/src/x.py
@@ -1,3 +1,1 @@
-def dead():
-    pass
-
DIFF
    exit 1 ;;
  *check*--fix*--diff*)
    cat <<'DIFF'
--- a/src/x.py
+++ b/src/x.py
@@ -1,2 +1,2 @@
-import os, sys
+import os
+import sys
DIFF
    exit 1 ;;
  *check*--fix*)
    exit 0 ;;
  *)
    exit 0 ;;
esac
MOCK
  chmod +x "$1/ruff"
}

# Bare-path diff (no a/ b/ prefix) — exercises normalize_diff.
mock_ruff_bare_diff() {
  cat > "$1/ruff" <<'MOCK'
#!/usr/bin/env bash
case "$*" in
  *check*--fix*--diff*)
    cat <<'DIFF'
--- src/x.py
+++ src/x.py
@@ -1,1 +1,1 @@
-x=1
+x = 1
DIFF
    exit 1 ;;
  *check*--fix*) exit 0 ;;
esac
MOCK
  chmod +x "$1/ruff"
}

mock_ruff_clean() {
  cat > "$1/ruff" <<'MOCK'
#!/usr/bin/env bash
exit 0
MOCK
  chmod +x "$1/ruff"
}

# Mock biome that mutates files in cwd when invoked with --write.
# Exercises copy_and_diff: lintguini-fix snapshots REPO_ROOT, runs biome
# in the snapshot, the mock writes to the snapshot, then the script
# diffs the original against the snapshot and emits the result.
mock_biome_writes() {
  local mb="$1" semantic_marker="${2:-}"
  cat > "$mb/biome" <<MOCK
#!/usr/bin/env bash
case "\$*" in
  *check*--write*--unsafe*)
    if [[ -f src/render.js ]]; then
      printf 'const semantic_fix = true;\n' > src/render.js
    fi
    exit 0 ;;
  *check*--write*)
    if [[ -f src/render.js ]]; then
      printf 'const safe_fix = true;\n' > src/render.js
    fi
    exit 0 ;;
  *) exit 0 ;;
esac
MOCK
  chmod +x "$mb/biome"
}

mock_biome_clean() {
  cat > "$1/biome" <<'MOCK'
#!/usr/bin/env bash
exit 0
MOCK
  chmod +x "$1/biome"
}

mock_standardrb_anything() {
  cat > "$1/standardrb" <<'MOCK'
#!/usr/bin/env bash
exit 0
MOCK
  chmod +x "$1/standardrb"
}

# -- Runners ------------------------------------------------------------

run_fix_split() {
  local repo="$1" mockbin="$2"; shift 2
  local out_f="$TMPROOT/out.$$" err_f="$TMPROOT/err.$$"
  PATH="$mockbin:$PATH" bash "$SCRIPT" "$@" "$repo" >"$out_f" 2>"$err_f"
  local rc=$?
  STDOUT="$(cat "$out_f")"
  STDERR="$(cat "$err_f")"
  rm -f "$out_f" "$err_f"
  return $rc
}

# -- 1. Configured + safe-fixable + read-only ---------------------------

test_python_readonly_diff() {
  local repo; repo="$(mktmp_repo py-readonly)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_safe_diff "$mb"
  run_fix_split "$repo" "$mb"
  local rc=$?
  if [[ "$STDOUT" == *"--- a/src/x.py"* && "$STDOUT" == *"+++ b/src/x.py"* ]]; then
    note_pass
  else
    note_fail "py readonly: missing unified-diff headers. stdout: $STDOUT"
  fi
  if (( rc == 1 )); then note_pass
  else note_fail "py readonly: expected exit 1, got $rc"; fi
}

# 1b. Bare-path diff input is normalised to a/ b/ prefix.
test_python_readonly_normalize() {
  local repo; repo="$(mktmp_repo py-normalize)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_bare_diff "$mb"
  run_fix_split "$repo" "$mb"
  local rc=$?
  # Mock emits "--- src/x.py" / "+++ src/x.py" — script must rewrite
  # to "--- a/src/x.py" / "+++ b/src/x.py".
  if [[ "$STDOUT" == *"--- a/src/x.py"* && "$STDOUT" == *"+++ b/src/x.py"* ]]; then
    note_pass
  else
    note_fail "py normalize: bare paths not rewritten to a/ b/. stdout: $STDOUT"
  fi
  if (( rc == 1 )); then note_pass
  else note_fail "py normalize: expected exit 1, got $rc"; fi
}

