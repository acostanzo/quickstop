# -- 7. Idempotency on --apply -----------------------------------------

# Mock that "fixes" something on first call, then returns clean on second.
# State carried through a marker file in the mockbin's parent dir so
# the mock survives across invocations.
mock_ruff_idempotent() {
  local mb="$1" marker="$mb/.ruff-applied"
  cat > "$mb/ruff" <<MOCK
#!/usr/bin/env bash
MARKER="$marker"
case "\$*" in
  *check*--fix*--diff*)
    if [[ -f "\$MARKER" ]]; then
      exit 0
    fi
    cat <<'DIFF'
--- a/src/x.py
+++ b/src/x.py
@@ -1,1 +1,1 @@
-x=1
+x = 1
DIFF
    exit 1 ;;
  *check*--fix*)
    touch "\$MARKER"
    exit 0 ;;
esac
MOCK
  chmod +x "$mb/ruff"
}

test_apply_idempotent() {
  local repo; repo="$(mktmp_repo idempotent)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_idempotent "$mb"

  # First call — should fix.
  run_fix_split "$repo" "$mb" --apply
  local rc1=$?
  if [[ "$STDOUT" == "fixed src/x.py" && $rc1 -eq 0 ]]; then note_pass
  else note_fail "idempotent first call: stdout='$STDOUT' rc=$rc1"; fi

  # Second call — nothing to fix, no `fixed` lines, exit 0.
  run_fix_split "$repo" "$mb" --apply
  local rc2=$?
  if [[ -z "$STDOUT" && $rc2 -eq 0 ]]; then note_pass
  else note_fail "idempotent second call: stdout='$STDOUT' rc=$rc2"; fi
  if [[ "$STDERR" == *"python: no fixes available"* ]]; then note_pass
  else note_fail "idempotent second call: missing 'no fixes available' stderr. stderr: $STDERR"; fi
}

