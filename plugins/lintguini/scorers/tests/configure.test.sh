# -- 2. TOML merge correctness — pyproject.toml's non-ruff sections survive

test_toml_merge_correctness() {
  local repo; repo="$(mktmp_repo py-merge)"
  cat > "$repo/pyproject.toml" <<'EOF'
[tool.poetry]
name = "demo"
version = "0.1.0"
description = "demo project"

[tool.poetry.dependencies]
python = "^3.12"
EOF

  run_configure --language python --strict "$repo" >/dev/null

  if grep -q '^\[tool\.poetry\]' "$repo/pyproject.toml" \
     && grep -q '^\[tool\.poetry\.dependencies\]' "$repo/pyproject.toml" \
     && grep -q 'description = "demo project"' "$repo/pyproject.toml"; then
    note_pass
  else
    note_fail "TOML merge dropped non-ruff [tool.poetry*] sections"
  fi

  if grep -q '^\[tool\.ruff\]' "$repo/pyproject.toml" \
     && grep -q '^\[tool\.ruff\.lint\]' "$repo/pyproject.toml"; then
    note_pass
  else
    note_fail "TOML merge: [tool.ruff*] sections not landed"
  fi

  # Strict band marker present, no leftover lenient/minimal markers.
  if grep -q '^# Strict band' "$repo/pyproject.toml" \
     && ! grep -q '^# Lenient band' "$repo/pyproject.toml" \
     && ! grep -q '^# Minimal band' "$repo/pyproject.toml"; then
    note_pass
  else
    note_fail "TOML merge: band marker mismatch"
  fi
}

# -- 3. Ruby auto-detection — Gemfile mentions standard ------------------

test_ruby_auto_standardrb() {
  local repo; repo="$(mktmp_repo ruby-auto-standard)"
  cat > "$repo/Gemfile" <<'EOF'
source "https://rubygems.org"
gem "rails"
gem "standard", require: false
EOF

  local live; live="$(run_configure --language ruby --strict "$repo")"
  if [[ "$live" == *"wrote standard.yml"* ]]; then note_pass
  else note_fail "ruby auto-standardrb: expected 'standard.yml wrote', got: $live"; fi
  if [[ ! -f "$repo/.rubocop.yml" ]]; then note_pass
  else note_fail "ruby auto-standardrb: should not write .rubocop.yml"; fi
}

# --ruby-tool override flips the path.
test_ruby_tool_override() {
  local repo; repo="$(mktmp_repo ruby-override)"
  cat > "$repo/Gemfile" <<'EOF'
source "https://rubygems.org"
gem "rails"
EOF
  # Gemfile-default is rubocop; --ruby-tool standardrb overrides.
  run_configure --language ruby --strict --ruby-tool standardrb "$repo" >/dev/null
  if [[ -f "$repo/standard.yml" && ! -f "$repo/.rubocop.yml" ]]; then note_pass
  else note_fail "--ruby-tool standardrb override didn't take effect"; fi
}

# -- 4. Idempotency — back-to-back identical invocations no-op -----------

test_idempotency_back_to_back() {
  local repo; repo="$(mktmp_repo idem)"
  cat > "$repo/pyproject.toml" <<'EOF'
[project]
name = "demo"
EOF
  run_configure --language python --strict "$repo" >/dev/null
  local out2; out2="$(run_configure --language python --strict "$repo")"
  local out3; out3="$(run_configure --language python --strict "$repo")"
  if [[ "$out2" == *"skipped pyproject.toml (already configured)"* \
      && "$out3" == *"skipped pyproject.toml (already configured)"* ]]; then note_pass
  else note_fail "idempotency: second/third runs not skipped. out2='$out2' out3='$out3'"; fi
}

# -- 5. Re-band — strict -> lenient rewrites the band -------------------

test_reband() {
  local repo; repo="$(mktmp_repo reband)"
  cat > "$repo/pyproject.toml" <<'EOF'
[project]
name = "demo"
EOF
  run_configure --language python --strict "$repo" >/dev/null

  local out; out="$(run_configure --language python --lenient "$repo")"
  if [[ "$out" == *"updated pyproject.toml"* ]]; then note_pass
  else note_fail "re-band: expected 'updated pyproject.toml', got: $out"; fi

  if grep -q '^# Lenient band' "$repo/pyproject.toml" \
     && ! grep -q '^# Strict band' "$repo/pyproject.toml"; then
    note_pass
  else
    note_fail "re-band: band marker did not flip strict -> lenient"
  fi

  # Lenient ruff select is shorter.
  if grep -q 'select = \["E", "F", "I"\]' "$repo/pyproject.toml"; then note_pass
  else note_fail "re-band: lenient ruff select not present"; fi

  # Round-trip back to strict, then minimal — every step idempotent on
  # the next run. (Catches preamble-strip leaks.)
  run_configure --language python --strict "$repo"   >/dev/null
  run_configure --language python --minimal "$repo"  >/dev/null
  out="$(run_configure --language python --minimal "$repo")"
  if [[ "$out" == *"skipped pyproject.toml (already configured)"* ]]; then note_pass
  else note_fail "re-band cycle: post-minimal idempotency leak: $out"; fi
}

# -- 6. CI wiring --------------------------------------------------------

test_ci_github_actions() {
  local repo; repo="$(mktmp_repo ci-gh)"
  echo '{"name":"x"}' > "$repo/package.json"
  mkdir -p "$repo/.github/workflows"
  echo 'name: ci' > "$repo/.github/workflows/test.yml"

  local live; live="$(run_configure --language javascript --strict --ci "$repo")"
  if [[ "$live" == *"wrote .github/workflows/lintguini-lint.yml"* ]]; then note_pass
  else note_fail "ci-gh: expected workflow wrote, got: $live"; fi

  if grep -q '^name: lint$' "$repo/.github/workflows/lintguini-lint.yml" \
     && grep -q 'biome check' "$repo/.github/workflows/lintguini-lint.yml"; then
    note_pass
  else
    note_fail "ci-gh: workflow content missing name or lint step"
  fi

  local rerun; rerun="$(run_configure --language javascript --strict --ci "$repo")"
  if [[ "$rerun" == *"skipped .github/workflows/lintguini-lint.yml (already configured)"* ]]; then note_pass
  else note_fail "ci-gh idempotency: $rerun"; fi
}

test_ci_no_surface_warns() {
  local repo; repo="$(mktmp_repo ci-none)"
  echo '{"name":"x"}' > "$repo/package.json"

  # Capture stderr separately from stdout for the warning shape check.
  local stderr_capture
  stderr_capture="$(bash "$SCRIPT" --language javascript --strict --ci "$repo" 2>&1 1>/dev/null)"
  if [[ "$stderr_capture" == *"warning no CI surface detected"* ]]; then note_pass
  else note_fail "ci-none: expected warning on stderr, got: $stderr_capture"; fi

  if [[ -f "$repo/biome.json" ]]; then note_pass
  else note_fail "ci-none: biome.json should still be written without --ci"; fi
}

# -- 7. --dry-run never writes ------------------------------------------

test_dry_run_no_writes() {
  local repo; repo="$(mktmp_repo dryrun)"
  cat > "$repo/pyproject.toml" <<'EOF'
[project]
name = "demo"
EOF
  echo '{"name":"x"}' > "$repo/package.json"
  mkdir -p "$repo/.github/workflows"
  echo 'name: ci' > "$repo/.github/workflows/test.yml"

  # Snapshot pre-state.
  local pre_snapshot
  pre_snapshot="$(cd "$repo" && find . -type f | LC_ALL=C sort | xargs -I{} sha256sum {} 2>/dev/null)"

  run_configure --strict --ci --dry-run "$repo" >/dev/null

  local post_snapshot
  post_snapshot="$(cd "$repo" && find . -type f | LC_ALL=C sort | xargs -I{} sha256sum {} 2>/dev/null)"

  if [[ "$pre_snapshot" == "$post_snapshot" ]]; then note_pass
  else note_fail "--dry-run: working tree mutated"; fi
}

# -- 8. Triple-run determinism on dry-run output -------------------------

test_triple_run_determinism() {
  local repo; repo="$(mktmp_repo determinism)"
  cat > "$repo/pyproject.toml" <<'EOF'
[project]
name = "demo"
EOF
  local r1 r2 r3
  r1="$(run_configure --language python --strict --dry-run "$repo")"
  r2="$(run_configure --language python --strict --dry-run "$repo")"
  r3="$(run_configure --language python --strict --dry-run "$repo")"
  if [[ "$r1" == "$r2" && "$r2" == "$r3" ]]; then note_pass
  else note_fail "triple-run dry-run output diverged"; fi
}

# -- 9. Argument errors --------------------------------------------------

test_argument_errors() {
  local repo; repo="$(mktmp_repo args)"
  echo '{"name":"x"}' > "$repo/package.json"

  bash "$SCRIPT" --strict --lenient --language javascript "$repo" >/dev/null 2>&1
  local rc=$?
  if (( rc == 2 )); then note_pass
  else note_fail "mutually-exclusive flags: expected rc=2 got rc=$rc"; fi

  bash "$SCRIPT" --language ruby "$repo" >/dev/null 2>&1
  rc=$?
  if (( rc == 2 )); then note_pass
  else note_fail "--language not detected: expected rc=2 got rc=$rc"; fi

  bash "$SCRIPT" --language elixir "$repo" >/dev/null 2>&1
  rc=$?
  if (( rc == 2 )); then note_pass
  else note_fail "unknown --language: expected rc=2 got rc=$rc"; fi

  bash "$SCRIPT" --strict "$repo/does-not-exist" >/dev/null 2>&1
  rc=$?
  if (( rc == 2 )); then note_pass
  else note_fail "non-existent REPO_ROOT: expected rc=2 got rc=$rc"; fi
}

# -- run -----------------------------------------------------------------

test_python_round_trip
test_javascript_round_trip
test_typescript_round_trip
test_rust_round_trip
test_ruby_round_trip_rubocop
test_go_round_trip
test_toml_merge_correctness
test_ruby_auto_standardrb
test_ruby_tool_override
test_idempotency_back_to_back
test_reband
test_ci_github_actions
test_ci_no_surface_warns
test_dry_run_no_writes
test_triple_run_determinism
test_argument_errors

if (( FAIL == 0 )); then
  echo "configure.test.sh: PASS — $PASS checks"
  exit 0
else
  echo "configure.test.sh: FAIL — $PASS passed, $FAIL failed" >&2
  for msg in "${FAILURES[@]}"; do
    echo "  - $msg" >&2
  done
  exit 1
fi
