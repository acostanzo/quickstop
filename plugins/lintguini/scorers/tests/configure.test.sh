#!/usr/bin/env bash
# configure.test.sh — exercise bin/lintguini-configure.sh end-to-end
# across every rubric language plus the cross-cutting concerns the M2
# brief calls out: TOML merge correctness, ruby auto-detection,
# idempotency, re-banding, CI wiring, --dry-run, and triple-run
# determinism.
#
# Tests run against tempdir fixtures built ad-hoc per case (the
# scorers/tests/fixtures/ tree is hand-curated for the audit path
# and would mix concerns if reused for write-test fixtures). Each
# tempdir is cleaned via per-case trap so failures don't leave the
# host's /tmp littered.

set -uo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_ROOT="$(cd "$HERE/../.." && pwd)"
SCRIPT="$PLUGIN_ROOT/bin/lintguini-configure.sh"

PASS=0
FAIL=0
FAILURES=()

note_pass() { PASS=$((PASS + 1)); }
note_fail() {
  FAIL=$((FAIL + 1))
  FAILURES+=("$1")
}

if [[ ! -x "$SCRIPT" ]]; then
  echo "FATAL: $SCRIPT not executable" >&2
  exit 2
fi

PROVENANCE='Generated by lintguini — see plugins/pronto/references/roll-your-own/lint-posture.md for the source of truth.'

# mktmp_repo  — make a tempdir, register cleanup via the global TRAP
# accumulator, return the path.
TMPROOT="$(mktemp -d -t lintguini-configure-tests.XXXXXX)"
trap 'rm -rf "$TMPROOT"' EXIT

mktmp_repo() {
  local d
  d="$(mktemp -d -p "$TMPROOT" "$1.XXXXXX")"
  printf '%s' "$d"
}

run_configure() {
  bash "$SCRIPT" "$@" 2>&1
}

# -- 1. Per-language fresh-repo round-trip --------------------------------

# 1a. Python — dry-run preview, then live write, then idempotency.
test_python_round_trip() {
  local repo; repo="$(mktmp_repo py)"
  cat > "$repo/pyproject.toml" <<'EOF'
[project]
name = "demo"
version = "0.0.1"
EOF
  local dry; dry="$(run_configure --language python --strict --dry-run "$repo")"
  if [[ "$dry" == *"would updated pyproject.toml"* ]]; then note_pass
  else note_fail "python dry-run: expected 'would updated pyproject.toml', got: $dry"; fi
  # Dry-run must not touch the working tree.
  if [[ "$(cat "$repo/pyproject.toml")" == "[project]"* ]]; then note_pass
  else note_fail "python dry-run mutated working tree"; fi

  local live; live="$(run_configure --language python --strict "$repo")"
  if [[ "$live" == *"updated pyproject.toml"* ]]; then note_pass
  else note_fail "python live: expected 'updated pyproject.toml', got: $live"; fi

  # Provenance is the first non-blank line.
  local first; first="$(awk 'NF { sub(/^[[:space:]]+/, ""); print; exit }' "$repo/pyproject.toml")"
  if [[ "$first" == "# $PROVENANCE" ]]; then note_pass
  else note_fail "python provenance not first non-blank line: '$first'"; fi

  local rerun; rerun="$(run_configure --language python --strict "$repo")"
  if [[ "$rerun" == *"skipped pyproject.toml (already configured)"* ]]; then note_pass
  else note_fail "python idempotency: expected skipped, got: $rerun"; fi
}

# 1b. JavaScript — fresh write of biome.json, then idempotency.
test_javascript_round_trip() {
  local repo; repo="$(mktmp_repo js)"
  echo '{"name":"demo"}' > "$repo/package.json"

  local live; live="$(run_configure --language javascript --strict "$repo")"
  if [[ "$live" == *"wrote biome.json"* ]]; then note_pass
  else note_fail "js fresh write: expected 'wrote biome.json', got: $live"; fi

  local first_key; first_key="$(jq -r 'keys_unsorted[0] // ""' "$repo/biome.json")"
  if [[ "$first_key" == "_provenance" ]]; then note_pass
  else note_fail "js _provenance not first key: '$first_key'"; fi

  local rerun; rerun="$(run_configure --language javascript --strict "$repo")"
  if [[ "$rerun" == *"skipped biome.json (already configured)"* ]]; then note_pass
  else note_fail "js idempotency: expected skipped, got: $rerun"; fi
}

# 1c. TypeScript — biome.json + tsconfig.json.
test_typescript_round_trip() {
  local repo; repo="$(mktmp_repo ts)"
  echo '{"name":"demo"}' > "$repo/package.json"
  echo '{"compilerOptions":{"target":"ES2020"},"include":["src/**/*"]}' > "$repo/tsconfig.json"

  local live; live="$(run_configure --language typescript --strict "$repo")"
  if [[ "$live" == *"wrote biome.json"* && "$live" == *"updated tsconfig.json"* ]]; then note_pass
  else note_fail "ts: expected biome wrote + tsconfig updated, got: $live"; fi

  # tsconfig's preserved key must survive.
  local include; include="$(jq -r '.include[0]' "$repo/tsconfig.json")"
  if [[ "$include" == "src/**/*" ]]; then note_pass
  else note_fail "ts merge dropped 'include' key: $include"; fi

  # tsconfig _provenance first.
  local first_key; first_key="$(jq -r 'keys_unsorted[0] // ""' "$repo/tsconfig.json")"
  if [[ "$first_key" == "_provenance" ]]; then note_pass
  else note_fail "ts tsconfig _provenance not first key: '$first_key'"; fi

  local rerun; rerun="$(run_configure --language typescript --strict "$repo")"
  if [[ "$rerun" == *"skipped biome.json (already configured)"* \
      && "$rerun" == *"skipped tsconfig.json (already configured)"* ]]; then note_pass
  else note_fail "ts idempotency: expected skipped both, got: $rerun"; fi
}

# 1d. Rust — rustfmt.toml direct + Cargo.toml merge with [lints*].
test_rust_round_trip() {
  local repo; repo="$(mktmp_repo rust)"
  cat > "$repo/Cargo.toml" <<'EOF'
[package]
name = "demo"
version = "0.1.0"
edition = "2021"

[dependencies]
serde = "1"
EOF

  local live; live="$(run_configure --language rust --strict "$repo")"
  if [[ "$live" == *"wrote rustfmt.toml"* && "$live" == *"updated Cargo.toml"* ]]; then note_pass
  else note_fail "rust: expected rustfmt wrote + Cargo updated, got: $live"; fi

  # Preserved [package] section must remain.
  if grep -q '^\[package\]' "$repo/Cargo.toml" && grep -q '^name = "demo"' "$repo/Cargo.toml"; then
    note_pass
  else
    note_fail "rust merge: lost user's [package] section"
  fi

  # Fragment landed.
  if grep -q '^\[lints.rust\]' "$repo/Cargo.toml" && grep -q '^\[lints.clippy\]' "$repo/Cargo.toml"; then
    note_pass
  else
    note_fail "rust merge: [lints.*] sections missing from Cargo.toml"
  fi

  # rustfmt.toml provenance first.
  local first; first="$(awk 'NF { sub(/^[[:space:]]+/, ""); print; exit }' "$repo/rustfmt.toml")"
  if [[ "$first" == "# $PROVENANCE" ]]; then note_pass
  else note_fail "rust rustfmt provenance not first line: '$first'"; fi

  local rerun; rerun="$(run_configure --language rust --strict "$repo")"
  if [[ "$rerun" == *"skipped rustfmt.toml (already configured)"* \
      && "$rerun" == *"skipped Cargo.toml (already configured)"* ]]; then note_pass
  else note_fail "rust idempotency: expected skipped both, got: $rerun"; fi
}

