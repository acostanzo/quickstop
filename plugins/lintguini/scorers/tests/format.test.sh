# -- 4. Not configured -> empty-scope on stderr, exit 0 ----------------

test_not_configured() {
  local repo; repo="$(mktmp_repo unconfig)"
  local mb; mb="$(mktmp_mockbin)"
  echo '[project]' > "$repo/pyproject.toml"  # detected, NOT configured
  mock_ruff_diffs "$mb" src/x.py
  run_format_split "$repo" "$mb" --check
  local rc=$?
  if [[ -z "$STDOUT" ]]; then note_pass
  else note_fail "not-configured: stdout should be empty, got: $STDOUT"; fi
  if [[ "$STDERR" == *"python: not configured (run /lintguini:configure --language python)"* ]]; then
    note_pass
  else
    note_fail "not-configured: missing greppable empty-scope message. stderr: $STDERR"
  fi
  if (( rc == 0 )); then note_pass
  else note_fail "not-configured: expected exit 0, got $rc"; fi
}

# -- 5. Tool not on PATH -> stderr message, exit 3 ---------------------

test_tool_missing() {
  local repo; repo="$(mktmp_repo no-tool)"
  local mb; mb="$(mktmp_mockbin)"  # empty
  write_python_config "$repo"
  local out_f="$TMPROOT/out.$$" err_f="$TMPROOT/err.$$"
  PATH="$mb:/usr/bin:/bin" bash "$SCRIPT" --check "$repo" >"$out_f" 2>"$err_f"
  local rc=$?
  STDOUT="$(cat "$out_f")"; STDERR="$(cat "$err_f")"
  rm -f "$out_f" "$err_f"
  if [[ "$STDERR" == *"python: ruff not on PATH (install ruff)"* ]]; then note_pass
  else note_fail "tool-missing: missing stderr hint. stderr: $STDERR"; fi
  if (( rc == 3 )); then note_pass
  else note_fail "tool-missing: expected exit 3, got $rc"; fi
}

