#!/usr/bin/env bash
# format.test.sh — exercise bin/lintguini-format.sh end-to-end against
# mocked formatters on PATH. Two modes under test:
#   --check  -> diff-only, exit 1 when files would change, stdout
#               reports each file as `would format <path>`.
#   apply    -> rewrites files in place; stdout reports each modified
#               file as `formatted <path>`.
#
# Real formatters (ruff, biome, cargo, rubocop, standardrb, gofmt)
# aren't installed on the test box. Mocks emit the per-tool check
# output the per-tool parsers in lintguini-format.sh expect.

set -uo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_ROOT="$(cd "$HERE/../.." && pwd)"
SCRIPT="$PLUGIN_ROOT/bin/lintguini-format.sh"

PASS=0
FAIL=0
FAILURES=()

note_pass() { PASS=$((PASS + 1)); }
note_fail() {
  FAIL=$((FAIL + 1))
  FAILURES+=("$1")
}

if [[ ! -x "$SCRIPT" ]]; then
  echo "FATAL: $SCRIPT not executable" >&2
  exit 2
fi

PROVENANCE='Generated by lintguini — see plugins/pronto/references/roll-your-own/lint-posture.md for the source of truth.'

TMPROOT="$(mktemp -d -t lintguini-format-tests.XXXXXX)"
trap 'rm -rf "$TMPROOT"' EXIT

mktmp_repo() {
  local d
  d="$(mktemp -d -p "$TMPROOT" "$1.XXXXXX")"
  printf '%s' "$d"
}

mktmp_mockbin() {
  local d
  d="$(mktemp -d -p "$TMPROOT" "mockbin.XXXXXX")"
  printf '%s' "$d"
}

# Provenance-marked config writers (mirror lint.test.sh).

write_python_config() {
  cat > "$1/pyproject.toml" <<EOF
# $PROVENANCE
[tool.ruff]
line-length = 100

[tool.ruff.format]
quote-style = "double"
EOF
}

write_javascript_config() {
  cat > "$1/biome.json" <<EOF
{
  "_provenance": "$PROVENANCE",
  "_band": "strict",
  "\$schema": "https://biomejs.dev/schemas/latest/schema.json",
  "linter": { "enabled": true },
  "formatter": { "enabled": true, "indentWidth": 2 }
}
EOF
  echo '{"name":"demo"}' > "$1/package.json"
}

write_rust_config() {
  cat > "$1/rustfmt.toml" <<EOF
