# Mocks. Each tool needs to behave correctly for both check and apply
# invocations because the script calls check first then apply (in
# apply mode). The mocks branch on argv.

# mock_ruff_diffs <bindir> <file1> [<file2>...]
#   ruff format --check . -> "Would reformat: <f>" lines, exit 1
#   ruff format .         -> exit 0 (apply succeeded)
mock_ruff_diffs() {
  local mb="$1"; shift
  local files="$*"
  cat > "$mb/ruff" <<MOCK
#!/usr/bin/env bash
case "\$*" in
  *--check*)
$(for f in $files; do echo "    echo 'Would reformat: $f'"; done)
    exit 1
    ;;
  *format*)
    exit 0
    ;;
  *)
    exit 0
    ;;
esac
MOCK
  chmod +x "$mb/ruff"
}

mock_ruff_clean() {
  cat > "$1/ruff" <<'MOCK'
#!/usr/bin/env bash
exit 0
MOCK
  chmod +x "$1/ruff"
}

# mock_ruff_apply_failure — check shows diffs, apply exits non-zero
mock_ruff_apply_failure() {
  cat > "$1/ruff" <<'MOCK'
#!/usr/bin/env bash
case "$*" in
  *--check*)
    echo "Would reformat: src/x.py"
    exit 1 ;;
  *format*)
    echo "panic: filesystem error" >&2
    exit 2 ;;
esac
MOCK
  chmod +x "$1/ruff"
}

mock_biome_diffs() {
  local mb="$1"; shift
  local files="$*"
  cat > "$mb/biome" <<MOCK
#!/usr/bin/env bash
case "\$*" in
  *check*--formatter-enabled*|*check*--linter-enabled*)
$(for f in $files; do echo "    echo '::error title=format,file=$f,line=1,col=1::file would be reformatted'"; done)
    exit 1
    ;;
  *format*--write*)
    exit 0
    ;;
  *)
    exit 0
    ;;
esac
MOCK
  chmod +x "$mb/biome"
}

mock_cargo_diffs() {
