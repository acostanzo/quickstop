edition = "2021"
EOF
  cat > "$1/Cargo.toml" <<'EOF'
[package]
name = "demo"
version = "0.1.0"
EOF
}

write_ruby_rubocop_config() {
  cat > "$1/.rubocop.yml" <<EOF
