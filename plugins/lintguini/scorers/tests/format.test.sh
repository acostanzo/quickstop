run_format_split() {
  local repo="$1" mockbin="$2"; shift 2
  local out_f="$TMPROOT/out.$$" err_f="$TMPROOT/err.$$"
  PATH="$mockbin:$PATH" bash "$SCRIPT" "$@" "$repo" >"$out_f" 2>"$err_f"
  local rc=$?
  STDOUT="$(cat "$out_f")"
  STDERR="$(cat "$err_f")"
  rm -f "$out_f" "$err_f"
  return $rc
}

# -- 1. --check on tree with diffs -> `would format X`, exit 1 ---------

test_python_check_diffs() {
  local repo; repo="$(mktmp_repo py-check)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_diffs "$mb" src/auth.py src/api.py
  run_format_split "$repo" "$mb" --check
  local rc=$?
  if [[ "$STDOUT" == *"would format src/api.py"* \
      && "$STDOUT" == *"would format src/auth.py"* ]]; then note_pass
  else note_fail "py --check: missing would-format lines. stdout: $STDOUT"; fi
  if (( rc == 1 )); then note_pass
  else note_fail "py --check: expected exit 1, got $rc"; fi
}

