  else note_fail "scope: expected exit 1, got $rc"; fi
}

# -- 8. Triple-run determinism ------------------------------------------

test_triple_run_determinism_check() {
  local repo; repo="$(mktmp_repo determinism-check)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_diffs "$mb" src/a.py src/b.py src/c.py
  local r1 r2 r3
  r1="$(PATH="$mb:$PATH" bash "$SCRIPT" --check "$repo" 2>/dev/null)"
  r2="$(PATH="$mb:$PATH" bash "$SCRIPT" --check "$repo" 2>/dev/null)"
  r3="$(PATH="$mb:$PATH" bash "$SCRIPT" --check "$repo" 2>/dev/null)"
  if [[ "$r1" == "$r2" && "$r2" == "$r3" ]]; then note_pass
  else note_fail "triple-run --check: output diverged"; fi
}

