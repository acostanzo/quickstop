#!/usr/bin/env bash
# lint.test.sh — exercise bin/lintguini-lint.sh end-to-end against
# mocked linters on PATH. The contract under test is the projection
# from each tool's native JSON / GitHub-format output to the locked
# `<path>:<line>:<rule>:<message>` finding shape M5's
# score-lint-pass-rate.sh will parse.
#
# Real linters (ruff, biome, cargo, rubocop, standardrb, golangci-lint)
# aren't installed on the test box. Each test stages a tempdir on
# PATH containing a fake binary that emits known output, then
# verifies the script's projection. This is the same shape inkwell's
# tests use for tool-dispatch coverage.

set -uo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_ROOT="$(cd "$HERE/../.." && pwd)"
SCRIPT="$PLUGIN_ROOT/bin/lintguini-lint.sh"

PASS=0
FAIL=0
FAILURES=()

note_pass() { PASS=$((PASS + 1)); }
note_fail() {
  FAIL=$((FAIL + 1))
  FAILURES+=("$1")
}

if [[ ! -x "$SCRIPT" ]]; then
  echo "FATAL: $SCRIPT not executable" >&2
  exit 2
fi

PROVENANCE='Generated by lintguini — see plugins/pronto/references/roll-your-own/lint-posture.md for the source of truth.'

TMPROOT="$(mktemp -d -t lintguini-lint-tests.XXXXXX)"
trap 'rm -rf "$TMPROOT"' EXIT

mktmp_repo() {
  local d
  d="$(mktemp -d -p "$TMPROOT" "$1.XXXXXX")"
  printf '%s' "$d"
}

mktmp_mockbin() {
  local d
  d="$(mktemp -d -p "$TMPROOT" "mockbin.XXXXXX")"
  printf '%s' "$d"
}

# write_python_config <repo>
#   Plant a provenance-marked pyproject.toml so is_configured(python) -> true.
write_python_config() {
  cat > "$1/pyproject.toml" <<EOF
# $PROVENANCE
# Strict band — full rubric baseline.

[tool.ruff]
line-length = 100

[tool.ruff.lint]
select = ["E", "F", "I"]
EOF
}

write_javascript_config() {
  cat > "$1/biome.json" <<EOF
{
  "_provenance": "$PROVENANCE",
  "_band": "strict",
  "\$schema": "https://biomejs.dev/schemas/latest/schema.json",
  "linter": { "enabled": true, "rules": { "recommended": true } },
  "formatter": { "enabled": true, "indentWidth": 2 }
}
EOF
  echo '{"name":"demo"}' > "$1/package.json"
}

write_typescript_config() {
  write_javascript_config "$1"
  cat > "$1/tsconfig.json" <<EOF
{
  "_provenance": "$PROVENANCE",
  "_band": "strict",
  "compilerOptions": { "strict": true }
}
EOF
}

write_rust_config() {
  cat > "$1/rustfmt.toml" <<EOF
# $PROVENANCE
edition = "2021"
EOF
  cat > "$1/Cargo.toml" <<'EOF'
[package]
name = "demo"
version = "0.1.0"
EOF
}

write_ruby_rubocop_config() {
  cat > "$1/.rubocop.yml" <<EOF
# $PROVENANCE
AllCops:
  NewCops: enable
EOF
  cat > "$1/Gemfile" <<'EOF'
source "https://rubygems.org"
gem "rails"
EOF
}

write_ruby_standardrb_config() {
  cat > "$1/standard.yml" <<EOF
# $PROVENANCE
ruby_version: 3.3
EOF
  cat > "$1/Gemfile" <<'EOF'
source "https://rubygems.org"
gem "standard"
EOF
}

write_go_config() {
  cat > "$1/.golangci.yml" <<EOF
# $PROVENANCE
linters:
  enable: [errcheck]
EOF
  cat > "$1/go.mod" <<'EOF'
module example.com/demo
go 1.22
EOF
}

# Mock factories. Each writes a fake binary at <bindir>/<tool> that
# emits known output and exits with the right code.

mock_ruff_violations() {
  cat > "$1/ruff" <<'MOCK'
#!/usr/bin/env bash
cat <<'JSON'
[
  {"filename": "src/auth.py", "code": "F401", "message": "imported but unused: typing.Optional", "location": {"row": 67, "column": 1}},
  {"filename": "src/auth.py", "code": "E501", "message": "line too long (120 > 88 characters)", "location": {"row": 42, "column": 89}},
  {"filename": "src/api.py",  "code": "E711", "message": "comparison to None should be 'is None'", "location": {"row": 12, "column": 3}}
]
JSON
exit 1
MOCK
  chmod +x "$1/ruff"
}

mock_ruff_clean() {
  cat > "$1/ruff" <<'MOCK'
#!/usr/bin/env bash
echo "[]"
exit 0
MOCK
  chmod +x "$1/ruff"
}

