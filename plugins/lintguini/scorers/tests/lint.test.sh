# run_lint <repo> <mockbin> [extra script flags...]
run_lint() {
  local repo="$1" mockbin="$2"; shift 2
  PATH="$mockbin:$PATH" bash "$SCRIPT" "$@" "$repo" 2>&1
}

run_lint_split() {
  # Run script, capture stdout and stderr separately. Output:
  #   stdout=...
  #   stderr=...
  # via tempfiles.
  local repo="$1" mockbin="$2"; shift 2
  local out_f="$TMPROOT/out.$$" err_f="$TMPROOT/err.$$"
  PATH="$mockbin:$PATH" bash "$SCRIPT" "$@" "$repo" >"$out_f" 2>"$err_f"
  local rc=$?
  STDOUT="$(cat "$out_f")"
  STDERR="$(cat "$err_f")"
  rm -f "$out_f" "$err_f"
  return $rc
}

# -- 1. Per-language: configured + violations -> projected findings ------

test_python_violations() {
  local repo; repo="$(mktmp_repo py-violations)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_violations "$mb"
  run_lint_split "$repo" "$mb"
  local rc=$?
  local expected
  expected=$'src/api.py:12:E711:comparison to None should be \'is None\'\nsrc/auth.py:42:E501:line too long (120 > 88 characters)\nsrc/auth.py:67:F401:imported but unused: typing.Optional'
  if [[ "$STDOUT" == "$expected" ]]; then note_pass
  else note_fail "python projection mismatch. got: $STDOUT"; fi
  if (( rc == 1 )); then note_pass
  else note_fail "python violations: expected exit 1, got $rc"; fi
}

