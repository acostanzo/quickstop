# -- 3. Not configured -> empty-scope on stderr, exit 0 -----------------

test_not_configured_python() {
  local repo; repo="$(mktmp_repo py-unconfig)"
  local mb; mb="$(mktmp_mockbin)"
  # python is detected via pyproject.toml but NOT lintguini-managed
  echo '[project]' > "$repo/pyproject.toml"
  mock_ruff_violations "$mb"
  run_lint_split "$repo" "$mb"
  local rc=$?
  if [[ -z "$STDOUT" ]]; then note_pass
  else note_fail "not-configured: stdout should be empty, got: $STDOUT"; fi
  if [[ "$STDERR" == *"python: not configured (run /lintguini:configure --language python)"* ]]; then
    note_pass
  else
    note_fail "not-configured: missing greppable empty-scope message. stderr: $STDERR"
  fi
  if (( rc == 0 )); then note_pass
  else note_fail "not-configured: expected exit 0, got $rc"; fi
}

# -- 4. Tool not on PATH -> stderr message, exit 3 ----------------------

test_tool_missing_python() {
  local repo; repo="$(mktmp_repo py-no-tool)"
  local mb; mb="$(mktmp_mockbin)"  # empty — no ruff in here
  write_python_config "$repo"
  # Use a sanitized PATH that excludes whatever real ruff might be installed.
  local out_f="$TMPROOT/out.$$" err_f="$TMPROOT/err.$$"
  PATH="$mb:/usr/bin:/bin" bash "$SCRIPT" "$repo" >"$out_f" 2>"$err_f"
  local rc=$?
  STDOUT="$(cat "$out_f")"; STDERR="$(cat "$err_f")"
  rm -f "$out_f" "$err_f"
  if [[ "$STDERR" == *"python: ruff not on PATH (install ruff)"* ]]; then note_pass
  else note_fail "tool-missing: missing stderr hint. stderr: $STDERR"; fi
  if (( rc == 3 )); then note_pass
  else note_fail "tool-missing: expected exit 3, got $rc"; fi
}

# -- 5. Polyglot — multiple configured langs, sectioned output ----------

test_polyglot_sections() {
  local repo; repo="$(mktmp_repo polyglot)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  write_javascript_config "$repo"
  mock_ruff_violations "$mb"
  mock_biome_violations "$mb"
  run_lint_split "$repo" "$mb"
  local rc=$?
  if [[ "$STDOUT" == *"# python — 3 findings"* \
      && "$STDOUT" == *"# javascript — 1 finding"* ]]; then
    note_pass
  else
    note_fail "polyglot: missing section headers. stdout: $STDOUT"
  fi
  # Findings appear after their respective headers.
  if [[ "$STDOUT" == *"src/auth.py:42:E501"* \
      && "$STDOUT" == *"src/render.js:88:lint/correctness/noUnusedVariables"* ]]; then
    note_pass
  else
    note_fail "polyglot: missing per-language findings. stdout: $STDOUT"
  fi
  if (( rc == 1 )); then note_pass
  else note_fail "polyglot: expected exit 1, got $rc"; fi
}

# -- 6. --language scopes to one language; no header --------------------

test_language_scope() {
  local repo; repo="$(mktmp_repo polyglot-scoped)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  write_javascript_config "$repo"
  mock_ruff_violations "$mb"
  mock_biome_violations "$mb"
  run_lint_split "$repo" "$mb" --language python
  local rc=$?
  if [[ "$STDOUT" != *"#"*"javascript"* && "$STDOUT" == *"src/auth.py:42:E501"* ]]; then
    note_pass
  else
    note_fail "scope: javascript leaked or python missing. stdout: $STDOUT"
  fi
  # Single-language scope -> no header.
  if [[ "$STDOUT" != *"# python"* ]]; then note_pass
  else note_fail "scope: header should not appear with --language. stdout: $STDOUT"; fi
  if (( rc == 1 )); then note_pass
  else note_fail "scope: expected exit 1, got $rc"; fi
}

# -- 7. Triple-run determinism ------------------------------------------

test_triple_run_determinism() {
  local repo; repo="$(mktmp_repo determinism)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"
  mock_ruff_violations "$mb"
  local r1 r2 r3
  r1="$(PATH="$mb:$PATH" bash "$SCRIPT" "$repo" 2>/dev/null)"
  r2="$(PATH="$mb:$PATH" bash "$SCRIPT" "$repo" 2>/dev/null)"
  r3="$(PATH="$mb:$PATH" bash "$SCRIPT" "$repo" 2>/dev/null)"
  if [[ "$r1" == "$r2" && "$r2" == "$r3" ]]; then note_pass
  else note_fail "triple-run output diverged"; fi
}

# -- 8. --language argument errors --------------------------------------

test_argument_errors() {
  local repo; repo="$(mktmp_repo args)"
  local mb; mb="$(mktmp_mockbin)"
  write_python_config "$repo"  # python detected + configured

  PATH="$mb:$PATH" bash "$SCRIPT" --language ruby "$repo" >/dev/null 2>&1
  local rc=$?
  if (( rc == 2 )); then note_pass
  else note_fail "--language not detected: expected rc=2 got $rc"; fi

  PATH="$mb:$PATH" bash "$SCRIPT" --language elixir "$repo" >/dev/null 2>&1
  rc=$?
  if (( rc == 2 )); then note_pass
  else note_fail "unknown --language: expected rc=2 got $rc"; fi

  PATH="$mb:$PATH" bash "$SCRIPT" "$repo/does-not-exist" >/dev/null 2>&1
  rc=$?
  if (( rc == 2 )); then note_pass
  else note_fail "non-existent REPO_ROOT: expected rc=2 got $rc"; fi
}

# -- 9. Section header singular/plural ----------------------------------

test_section_header_singular() {
  local repo; repo="$(mktmp_repo singular)"
  local mb; mb="$(mktmp_mockbin)"
