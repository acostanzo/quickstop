{"reason":"compiler-message","message":{"code":{"code":"clippy::needless_return"},"level":"warning","message":"unneeded `return` statement","spans":[{"file_name":"src/main.rs","line_start":12,"column_start":5,"is_primary":true}]}}
{"reason":"build-script-executed","linked_libs":[]}
{"reason":"compiler-message","message":{"code":{"code":"clippy::redundant_clone"},"level":"warning","message":"redundant clone","spans":[{"file_name":"src/lib.rs","line_start":7,"column_start":1,"is_primary":true}]}}
