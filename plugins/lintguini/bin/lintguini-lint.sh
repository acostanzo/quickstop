#!/usr/bin/env bash
# lintguini-lint.sh — run the lintguini-configured linter(s) against a
# target consumer repo and emit structured findings as
#
#     <path>:<line>:<rule>:<message>
#
# one finding per line on stdout. The shape is the locked T3 contract
# downstream tooling depends on — most notably the M5
# score-lint-pass-rate.sh scorer, which parses this output to grade
# pass rate. Adding a column or reordering fields is a breaking change.
#
# Determinism contract: this is the deterministic half of
# /lintguini:lint. The skill body is the LLM-shaped half (intent
# reading, summary surfacing) and lives at skills/lint/SKILL.md.
# Same REPO_ROOT state + same flags + same tool versions on PATH ->
# byte-equivalent stdout across runs. Findings are sorted by
# (path, line, column, rule, message) so the projection is stable.
#
# ADR-006 §2: lint is a capability surface invoked by the consumer; it
# never mutates the consumer's working tree. The dispatched linters
# read source files and emit diagnostics — they do not write.
#
# ADR-008: the rubric pins the canonical tool per language. We dispatch
# that tool deterministically — no fallback to a "second-best" linter —
# because falling back would mean grading a repo against a different
# rubric than the one /lintguini:configure wrote it for.
#
# Per-language tool dispatch:
#
#   python      ruff check --output-format=json .
#   javascript  biome check --reporter=github
#   typescript  biome check --reporter=github
#   rust        cargo clippy --message-format=json --quiet -- -D warnings
#   ruby        rubocop --format json . | standardrb --format json .
#   go          golangci-lint run --out-format=json
#
# Projection from each tool's native JSON / GitHub-style output into
# the locked path:line:rule:message shape happens in the per-language
# `lint_<lang>` functions. Mock-tool tests at
# scorers/tests/lint.test.sh exercise the projection deterministically
# without any real linter on PATH.
#
# Usage:
#   lintguini-lint.sh [--language <lang>] <REPO_ROOT>
#
# Flags:
#   --language <lang>  Scope to a single language (one of:
#                      python, javascript, typescript, rust, ruby, go).
#                      Without it, lint every detected + configured
#                      language. With it, the language must be
#                      detected — undetected langs are an argument
#                      error (exit 2).
#   -h | --help        Show this help.
#
# Polyglot output:
#   When two or more configured languages are linted in a single run,
#   stdout is sectioned by language with a metadata header line:
#
#     # python — 3 findings
#     src/auth.py:42:E501:line too long (120 > 88 characters)
#     ...
#     # javascript — 1 finding
#     src/render.js:88:lint/suspicious/noConsole:Unexpected console statement
#
#   The header line begins with `# ` and is the only non-finding line
#   on stdout — score-lint-pass-rate.sh (T5) skips lines matching
#   ^# to count findings. Single-language runs (configured=1 or
#   --language <lang>) emit no header.
#
# Empty-scope and tool-missing diagnostics go to stderr, never stdout:
#
#   <language>: not configured (run /lintguini:configure --language <lang>)
#   <language>: <tool> not on PATH (install <tool>)
#
# Exit codes:
#   0  no findings; or empty-scope on every in-scope language
#   1  one or more findings emitted to stdout
#   2  argument errors (unknown flag, unsupported --language,
#      --language scoped to a non-detected language, missing REPO_ROOT)
#   3  required tooling missing on PATH for an in-scope language
#   4  linter execution failure (tool crashed, returned an
#      unrecognised exit code, or emitted unparseable output)

set -uo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_ROOT="$(cd "$HERE/.." && pwd)"
DETECT_BIN="$PLUGIN_ROOT/bin/lintguini-detect-language.sh"

PROVENANCE_MARK='Generated by lintguini'

usage() {
  sed -n '/^# Usage:/,/^# Exit codes:/p' "$0" | sed 's/^# \{0,1\}//' >&2
}

die() {
  local msg="$1" rc="${2:-2}"
  echo "lintguini-lint: $msg" >&2
  exit "$rc"
}

# -- argument parsing ----------------------------------------------------

LANGUAGE=""
REPO_ROOT_ARG=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --language)
      [[ $# -ge 2 ]] || die "--language requires a value"
      LANGUAGE="$2"; shift 2 ;;
    --language=*)
      LANGUAGE="${1#--language=}"; shift ;;
    -h|--help)
      usage; exit 0 ;;
    --)
      shift
      while [[ $# -gt 0 ]]; do
        [[ -n "$REPO_ROOT_ARG" ]] && die "unexpected positional '$1'"
        REPO_ROOT_ARG="$1"; shift
      done ;;
    --*)
      die "unknown flag '$1'" ;;
    *)
      [[ -n "$REPO_ROOT_ARG" ]] && die "unexpected positional '$1'"
      REPO_ROOT_ARG="$1"; shift ;;
  esac
done

if [[ -n "$LANGUAGE" ]]; then
  case "$LANGUAGE" in
    python|javascript|typescript|rust|ruby|go) ;;
    *) die "--language must be one of: python javascript typescript rust ruby go (got '$LANGUAGE')" ;;
  esac
fi

[[ -z "$REPO_ROOT_ARG" ]] && { usage; die "REPO_ROOT positional argument required"; }
[[ -d "$REPO_ROOT_ARG" ]] || die "REPO_ROOT '$REPO_ROOT_ARG' is not a directory"
REPO_ROOT="$(cd "$REPO_ROOT_ARG" && pwd)"

# -- tooling check (host-side, not target linters) -----------------------

command -v jq >/dev/null 2>&1 || die "jq is required" 3

# -- detection -----------------------------------------------------------

mapfile -t DETECTED < <("$DETECT_BIN" "$REPO_ROOT")

if [[ -n "$LANGUAGE" ]]; then
  found=0
  for l in "${DETECTED[@]}"; do
    [[ "$l" == "$LANGUAGE" ]] && { found=1; break; }
  done
  (( found == 1 )) || die "--language $LANGUAGE not detected in $REPO_ROOT (detected: ${DETECTED[*]:-none})"
  TARGET_LANGS=("$LANGUAGE")
else
  if [[ ${#DETECTED[@]} -eq 0 ]]; then
    # No language markers at all — empty-scope across the board.
    echo "lintguini: no supported languages detected (need pyproject.toml/Cargo.toml/go.mod/Gemfile/.rubocop.yml/standard.yml/.standard.yml/tsconfig.json/package.json)" >&2
    exit 0
  fi
  TARGET_LANGS=("${DETECTED[@]}")
fi

# -- config-presence detection -------------------------------------------

# is_configured <lang>
#   Return 0 iff the language has a lintguini-managed config in REPO_ROOT.
#   The marker is the provenance string lintguini-configure writes into
#   every config it produces — same idempotency signal /lintguini:configure
#   uses. Per-language anchor file:
#     python       pyproject.toml (ruff config + provenance)
#     javascript   biome.json with _provenance key
#     typescript   biome.json with _provenance key
#     rust         rustfmt.toml (always written by configure for Rust;
#                  Cargo.toml [lints] is band-conditional, rustfmt.toml
#                  is band-unconditional)
#     ruby         standard.yml or .rubocop.yml with provenance
#     go           .golangci.yml with provenance
is_configured() {
  local lang="$1"
  case "$lang" in
    python)
      [[ -f "$REPO_ROOT/pyproject.toml" ]] \
        && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/pyproject.toml"
      ;;
    javascript|typescript)
      [[ -f "$REPO_ROOT/biome.json" ]] \
        && jq -e --arg m "$PROVENANCE_MARK" \
            '(._provenance // "") | contains($m)' "$REPO_ROOT/biome.json" >/dev/null 2>&1
      ;;
    rust)
      [[ -f "$REPO_ROOT/rustfmt.toml" ]] \
        && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/rustfmt.toml"
      ;;
    ruby)
      ( [[ -f "$REPO_ROOT/standard.yml" ]] && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/standard.yml" ) \
        || ( [[ -f "$REPO_ROOT/.rubocop.yml" ]] && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/.rubocop.yml" )
      ;;
    go)
      [[ -f "$REPO_ROOT/.golangci.yml" ]] \
        && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/.golangci.yml"
      ;;
    *)
      return 1
      ;;
  esac
}

# ruby_tool
#   Pick the ruby tool to dispatch based on the provenance-marked
#   config present in REPO_ROOT. standard.yml wins when both exist
#   because /lintguini:configure --ruby-tool standardrb leaves a stale
#   .rubocop.yml in place (configure does not delete prior selections).
ruby_tool() {
  if [[ -f "$REPO_ROOT/standard.yml" ]] && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/standard.yml"; then
    echo "standardrb"
  else
    echo "rubocop"
  fi
}

# -- per-language linters ------------------------------------------------
#
# Contract: each lint_<lang> function:
#   1. Confirms the canonical tool is on PATH (exit 3 with a stderr
#      hint if not).
#   2. Runs the tool from inside REPO_ROOT capturing stdout.
#   3. Validates the tool's output is parseable; on parse failure
#      exits 4 with a stderr hint.
#   4. Projects the tool's output to one path:line:rule:message line
#      per finding via jq (or sed for biome's GitHub-format).
#   5. Sorts findings by (path, line, rule, message) for stable
#      cross-run output (column is not in the contract, so it isn't
#      one of the sort keys).
#   6. Prints findings to stdout — one per line, no header. The main
#      loop emits headers when polyglot. Empty stdout means no findings.
#
# Functions are invoked inside command substitution (`out=$(lint_x)`),
# so the count is recomputed in the parent from $out's non-empty line
# count rather than smuggled out via a global (subshell isolation
# would lose it).

# tool_missing <lang> <tool>
tool_missing() {
  echo "$1: $2 not on PATH (install $2)" >&2
  exit 3
}

# tool_failure <lang> <tool> <hint>
tool_failure() {
  echo "$1: $2 execution failed — $3" >&2
  exit 4
}

# strip_root <abs-or-rel-path>
#   Render a path repo-relative. Tools emit a mix: ruff with `.` argument
#   gives `./src/x.py`, clippy gives workspace-relative `src/main.rs`,
#   biome github-format gives the path as supplied. We normalise here.
strip_root() {
  local p="$1"
  # Drop leading ./
  p="${p#./}"
  # Drop REPO_ROOT prefix when present (with or without trailing slash).
  case "$p" in
    "$REPO_ROOT"/*) p="${p#$REPO_ROOT/}" ;;
    "$REPO_ROOT")   p="." ;;
  esac
  printf '%s' "$p"
}

# sort_findings
#   Read findings on stdin, sort stably by all four fields. Findings
#   are always `<path>:<line>:<rule>:<message>` so we pin -t: with -k
#   path, then -k numeric line, then -k rule, then -k message. LC_ALL=C
#   for byte-deterministic ordering across locales.
sort_findings() {
  LC_ALL=C sort -t: -k1,1 -k2,2n -k3,3 -k4
}

lint_python() {
  command -v ruff >/dev/null 2>&1 || tool_missing python ruff
  local out rc
  out="$(cd "$REPO_ROOT" && ruff check --output-format=json . 2>/dev/null)"
  rc=$?
  # ruff exits 0 = clean, 1 = findings, 2 = error. JSON is emitted on
  # 0 and 1; 2 means the linter itself failed (e.g. config error).
  if (( rc != 0 && rc != 1 )); then
    tool_failure python ruff "exit $rc"
  fi
  [[ -z "$out" ]] && out="[]"
  if ! echo "$out" | jq -e 'type == "array"' >/dev/null 2>&1; then
    tool_failure python ruff "unparseable JSON"
  fi
  local findings
  findings="$(echo "$out" | jq -r '
    .[] |
    [(.filename // ""), (.location.row // 0), (.code // ""), (.message // "" | gsub("\n"; " "))] |
    @tsv
  ')"
  [[ -z "$findings" ]] && return 0
  echo "$findings" | while IFS=$'\t' read -r f r c m; do
    [[ -z "$f" ]] && continue
    rel="$(strip_root "$f")"
    printf '%s:%s:%s:%s\n' "$rel" "$r" "$c" "$m"
  done | sort_findings
}

lint_biome() {
  local lang="$1"
  command -v biome >/dev/null 2>&1 || tool_missing "$lang" biome
  local out rc
  out="$(cd "$REPO_ROOT" && biome check --reporter=github . 2>/dev/null)"
  rc=$?
  # biome exits 0 = clean, 1 = findings or error. We accept both;
  # parse failure surfaces below.
  if (( rc != 0 && rc != 1 )); then
    tool_failure "$lang" biome "exit $rc"
  fi
  # GitHub-format lines:
  #   ::warning title=lint/cat/rule,file=path,line=N,col=N,endLine=N,endCol=N::message
  # Empty output when clean.
  [[ -z "$out" ]] && return 0
  echo "$out" \
    | sed -n 's|^::[a-zA-Z]\+ \(.*\)::\(.*\)$|\1\t\2|p' \
    | while IFS=$'\t' read -r meta msg; do
        [[ -z "$meta" ]] && continue
        local title file line
        title="$(printf '%s' "$meta" | sed -n 's|.*title=\([^,]*\).*|\1|p')"
        file="$(printf '%s' "$meta" | sed -n 's|.*file=\([^,]*\).*|\1|p')"
        line="$(printf '%s' "$meta" | sed -n 's|.*line=\([^,]*\).*|\1|p')"
        [[ -z "$file" || -z "$line" ]] && continue
        # Default rule when biome elides the title (rare — defensive).
        [[ -z "$title" ]] && title="lint"
        rel="$(strip_root "$file")"
        printf '%s:%s:%s:%s\n' "$rel" "$line" "$title" "$msg"
      done | sort_findings
}

lint_javascript() { lint_biome javascript; }
lint_typescript() { lint_biome typescript; }

lint_rust() {
  command -v cargo >/dev/null 2>&1 || tool_missing rust cargo
  local out
  # clippy returns non-zero when warnings escalate to errors under
  # -D warnings. Both 0 and non-zero produce useful JSON; only treat
  # truly unparseable output as failure.
  out="$(cd "$REPO_ROOT" && cargo clippy --message-format=json --quiet -- -D warnings 2>/dev/null)"
  [[ -z "$out" ]] && return 0
  # Streaming jq: cargo emits one JSON object per line. Filter to
  # compiler-message entries with a code and a span; pick the
  # primary span when one is flagged, otherwise the first.
  local findings
  if ! findings="$(printf '%s\n' "$out" \
    | jq -r 'select(.reason == "compiler-message")
            | select(.message.code.code != null)
            | .message as $m
            | (($m.spans // []) | map(select(.is_primary == true)) | (.[0] // (($m.spans // [])[0] // null))) as $s
            | select($s != null)
            | [($s.file_name // ""), ($s.line_start // 0), ($m.code.code // ""), ($m.message // "" | gsub("\n"; " "))]
            | @tsv')"; then
    tool_failure rust "cargo clippy" "unparseable JSON"
  fi
  [[ -z "$findings" ]] && return 0
  printf '%s\n' "$findings" | while IFS=$'\t' read -r f r c m; do
    [[ -z "$f" ]] && continue
    rel="$(strip_root "$f")"
    printf '%s:%s:%s:%s\n' "$rel" "$r" "$c" "$m"
  done | sort_findings
}

lint_ruby() {
  local tool; tool="$(ruby_tool)"
  command -v "$tool" >/dev/null 2>&1 || tool_missing ruby "$tool"
  local out rc
  out="$(cd "$REPO_ROOT" && "$tool" --format json . 2>/dev/null)"
  rc=$?
  # rubocop / standardrb: 0 = clean, 1 = findings, 2 = internal error.
  if (( rc != 0 && rc != 1 )); then
    tool_failure ruby "$tool" "exit $rc"
  fi
  [[ -z "$out" ]] && out='{"files":[]}'
  if ! echo "$out" | jq -e 'type == "object"' >/dev/null 2>&1; then
    tool_failure ruby "$tool" "unparseable JSON"
  fi
  local findings
  findings="$(echo "$out" | jq -r '
    .files[]? as $f
    | $f.offenses[]?
    | [($f.path // ""), (.location.start_line // .location.line // 0), (.cop_name // ""), (.message // "" | gsub("\n"; " "))]
    | @tsv
  ')"
  [[ -z "$findings" ]] && return 0
  echo "$findings" | while IFS=$'\t' read -r f r c m; do
    [[ -z "$f" ]] && continue
    rel="$(strip_root "$f")"
    printf '%s:%s:%s:%s\n' "$rel" "$r" "$c" "$m"
  done | sort_findings
}

lint_go() {
  command -v golangci-lint >/dev/null 2>&1 || tool_missing go golangci-lint
  local out rc
  out="$(cd "$REPO_ROOT" && golangci-lint run --out-format=json ./... 2>/dev/null)"
  rc=$?
  if (( rc != 0 && rc != 1 )); then
    tool_failure go golangci-lint "exit $rc"
  fi
  [[ -z "$out" ]] && out='{"Issues":[]}'
  if ! echo "$out" | jq -e 'type == "object"' >/dev/null 2>&1; then
    tool_failure go golangci-lint "unparseable JSON"
  fi
  local findings
  findings="$(echo "$out" | jq -r '
    (.Issues // [])[]
    | [(.Pos.Filename // ""), (.Pos.Line // 0), (.FromLinter // ""), (.Text // "" | gsub("\n"; " "))]
    | @tsv
  ')"
  [[ -z "$findings" ]] && return 0
  echo "$findings" | while IFS=$'\t' read -r f r c m; do
    [[ -z "$f" ]] && continue
    rel="$(strip_root "$f")"
    printf '%s:%s:%s:%s\n' "$rel" "$r" "$c" "$m"
  done | sort_findings
}

# -- main loop -----------------------------------------------------------
#
# Two-phase: filter to configured languages first (so the polyglot
# header math knows whether headers are needed), then dispatch each.

CONFIGURED_LANGS=()
for lang in "${TARGET_LANGS[@]}"; do
  if is_configured "$lang"; then
    CONFIGURED_LANGS+=("$lang")
  else
    echo "$lang: not configured (run /lintguini:configure --language $lang)" >&2
  fi
done

if [[ ${#CONFIGURED_LANGS[@]} -eq 0 ]]; then
  # Every in-scope language was empty-scope — exit 0 cleanly.
  exit 0
fi

# Headers iff multiple sections.
EMIT_HEADERS=0
(( ${#CONFIGURED_LANGS[@]} > 1 )) && EMIT_HEADERS=1

TOTAL_FINDINGS=0
for lang in "${CONFIGURED_LANGS[@]}"; do
  case "$lang" in
    python)     out="$(lint_python)";     lint_rc=$? ;;
    javascript) out="$(lint_javascript)"; lint_rc=$? ;;
    typescript) out="$(lint_typescript)"; lint_rc=$? ;;
    rust)       out="$(lint_rust)";       lint_rc=$? ;;
    ruby)       out="$(lint_ruby)";       lint_rc=$? ;;
    go)         out="$(lint_go)";         lint_rc=$? ;;
    *)          continue ;;
  esac
  # tool_missing / tool_failure exit the subshell with 3 / 4. Propagate
  # those rc's up so the parent terminates with the right code instead
  # of swallowing them and continuing as if nothing happened.
  if (( lint_rc == 3 || lint_rc == 4 )); then
    exit "$lint_rc"
  fi
  # Count is recomputed from $out (subshell isolation prevents lint_*
  # from sharing a counter back through globals). Empty $out -> 0.
  if [[ -z "$out" ]]; then
    count=0
  else
    count=$(printf '%s\n' "$out" | grep -c '.' || true)
  fi
  if (( EMIT_HEADERS == 1 )); then
    if (( count == 1 )); then
      printf '# %s — 1 finding\n' "$lang"
    else
      printf '# %s — %s findings\n' "$lang" "$count"
    fi
  fi
  if [[ -n "$out" ]]; then
    printf '%s\n' "$out"
  fi
  TOTAL_FINDINGS=$((TOTAL_FINDINGS + count))
done

if (( TOTAL_FINDINGS > 0 )); then
  exit 1
fi
exit 0
