#!/usr/bin/env bash
# lintguini-configure.sh — write rubric-projected lint config files
# into a target consumer repo.
#
# Detects the language(s) present (via lintguini-detect-language.sh),
# picks a strictness band (--strict | --lenient | --minimal), and
# materialises the matching templates from plugins/lintguini/templates/
# at the conventional locations in the consumer's repo. Optionally
# wires a lint step into the consumer's most-conventional CI surface
# via --ci.
#
# Determinism contract: this is the deterministic half of
# /lintguini:configure. Same flags + same REPO_ROOT state -> same
# bytes on disk. The skill body is the LLM-shaped half (intent
# reading, summary surfacing) and lives at skills/configure/SKILL.md.
#
# ADR-008 §1.4: every config we write is self-describing — the
# provenance comment names the rubric reference. ADR-006 §2: this is
# a capability surface the consumer invoked, not a hook that mutates
# behind their back; we only touch files documented by the brief.
#
# Usage:
#   lintguini-configure.sh [flags] <REPO_ROOT>
#
# Flags:
#   --language <lang>        Scope to a single language
#                            (python|javascript|typescript|rust|ruby|go).
#                            Without it, configure every detected language.
#   --strict | --lenient | --minimal
#                            Strictness band (default: --strict).
#                            Mutually exclusive.
#   --ruby-tool rubocop|standardrb
#                            Ruby tool selection. Without it, auto-detect
#                            from Gemfile (gem "standard" -> standardrb,
#                            otherwise rubocop).
#   --ci                     Wire a lint step into the detected CI surface.
#   --dry-run                Print "would <verb> <path>"; do not modify any files.
#   -h | --help              Show this help.
#
# Output verbs (one per line on stdout):
#   wrote <path>             — destination did not exist; new file
#   updated <path>           — destination existed; content changed
#   skipped <path> (already configured)
#                            — destination exists with byte-equivalent content
#   would wrote / would updated / would skipped
#                            — dry-run preview (mirrors the live verbs)
#   warning <message>        — non-fatal diagnostic to stderr
#
# Exit codes:
#   0  success (or dry-run completed)
#   2  argument errors (mutually-exclusive flags, unknown language,
#      --language scoped to a language not detected, malformed flags)
#   3  required tooling missing (jq, python3 with tomllib)
#   4  write failure (permission, mv, etc.)

set -uo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_ROOT="$(cd "$HERE/.." && pwd)"
TEMPLATES="$PLUGIN_ROOT/templates"
DETECT_BIN="$PLUGIN_ROOT/bin/lintguini-detect-language.sh"

PROVENANCE_TEXT='Generated by lintguini — see plugins/pronto/references/roll-your-own/lint-posture.md for the source of truth.'

usage() {
  sed -n '/^# Usage:/,/^# Exit codes:/p' "$0" | sed 's/^# \{0,1\}//' >&2
}

die() {
  local msg="$1" rc="${2:-2}"
  echo "lintguini-configure: $msg" >&2
  exit "$rc"
}

warn() {
  echo "warning $1" >&2
}

# -- argument parsing ----------------------------------------------------

LANGUAGE=""
BAND=""
RUBY_TOOL=""
CI=0
DRY_RUN=0
REPO_ROOT_ARG=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --language)
      [[ $# -ge 2 ]] || die "--language requires a value"
      LANGUAGE="$2"; shift 2 ;;
    --language=*)
      LANGUAGE="${1#--language=}"; shift ;;
    --strict|--lenient|--minimal)
      if [[ -n "$BAND" ]]; then
        die "--strict, --lenient, --minimal are mutually exclusive"
      fi
      BAND="${1#--}"; shift ;;
    --ruby-tool)
      [[ $# -ge 2 ]] || die "--ruby-tool requires a value"
      RUBY_TOOL="$2"; shift 2 ;;
    --ruby-tool=*)
      RUBY_TOOL="${1#--ruby-tool=}"; shift ;;
    --ci)
      CI=1; shift ;;
    --dry-run)
      DRY_RUN=1; shift ;;
    -h|--help)
      usage; exit 0 ;;
    --)
      shift
      while [[ $# -gt 0 ]]; do
        if [[ -n "$REPO_ROOT_ARG" ]]; then die "unexpected positional '$1'"; fi
        REPO_ROOT_ARG="$1"; shift
      done ;;
    --*)
      die "unknown flag '$1'" ;;
    *)
      if [[ -n "$REPO_ROOT_ARG" ]]; then die "unexpected positional '$1'"; fi
      REPO_ROOT_ARG="$1"; shift ;;
  esac
done

[[ -z "$BAND" ]] && BAND="strict"

if [[ -n "$RUBY_TOOL" ]]; then
  case "$RUBY_TOOL" in
    rubocop|standardrb) ;;
    *) die "--ruby-tool must be 'rubocop' or 'standardrb' (got '$RUBY_TOOL')" ;;
  esac
fi

if [[ -n "$LANGUAGE" ]]; then
  case "$LANGUAGE" in
    python|javascript|typescript|rust|ruby|go) ;;
    *) die "--language must be one of: python javascript typescript rust ruby go (got '$LANGUAGE')" ;;
  esac
fi

[[ -z "$REPO_ROOT_ARG" ]] && { usage; die "REPO_ROOT positional argument required"; }
[[ -d "$REPO_ROOT_ARG" ]] || die "REPO_ROOT '$REPO_ROOT_ARG' is not a directory"
REPO_ROOT="$(cd "$REPO_ROOT_ARG" && pwd)"

# -- tooling check -------------------------------------------------------

command -v jq >/dev/null 2>&1 || die "jq is required" 3
command -v python3 >/dev/null 2>&1 || die "python3 is required" 3
python3 -c 'import tomllib' >/dev/null 2>&1 \
  || die "python3 must be 3.11+ (tomllib required for TOML parsing)" 3

# -- language detection --------------------------------------------------

mapfile -t DETECTED < <("$DETECT_BIN" "$REPO_ROOT")
if [[ ${#DETECTED[@]} -eq 0 ]]; then
  die "no supported languages detected in $REPO_ROOT (need pyproject.toml/Cargo.toml/go.mod/Gemfile/.rubocop.yml/standard.yml/.standard.yml/tsconfig.json/package.json)"
fi

if [[ -n "$LANGUAGE" ]]; then
  found=0
  for l in "${DETECTED[@]}"; do
    [[ "$l" == "$LANGUAGE" ]] && { found=1; break; }
  done
  (( found == 1 )) || die "--language $LANGUAGE not detected in $REPO_ROOT (detected: ${DETECTED[*]:-none})"
  TARGET_LANGS=("$LANGUAGE")
else
  TARGET_LANGS=("${DETECTED[@]}")
fi

# Heads-up if --ruby-tool was supplied but we won't be configuring ruby this run.
if [[ -n "$RUBY_TOOL" ]]; then
  configuring_ruby=0
  for l in "${TARGET_LANGS[@]}"; do
    [[ "$l" == "ruby" ]] && configuring_ruby=1
  done
  (( configuring_ruby == 1 )) || warn "--ruby-tool ignored: ruby not in this run's target languages"
fi

# -- write helpers -------------------------------------------------------

# rel_path <abs-path>
#   Convert an absolute path under REPO_ROOT to a repo-relative one
#   for stable output. Any path outside REPO_ROOT is returned as-is.
rel_path() {
  case "$1" in
    "$REPO_ROOT"/*) printf '%s' "${1#$REPO_ROOT/}" ;;
    "$REPO_ROOT")   printf '.' ;;
    *)              printf '%s' "$1" ;;
  esac
}

# finalize_write <tmp> <dest>
#   Compare <tmp>'s bytes to <dest>'s bytes; emit the appropriate verb
#   (skipped/wrote/updated, optionally prefixed with "would " in dry-run);
#   in non-dry mode, mv <tmp> into place when content changed.
finalize_write() {
  local tmp="$1" dest="$2"
  local rel
  rel="$(rel_path "$dest")"

  local existed=0 verb="wrote"
  if [[ -e "$dest" ]]; then
    existed=1
    if cmp -s "$tmp" "$dest"; then
      rm -f "$tmp"
      echo "skipped $rel (already configured)"
      return 0
    fi
    verb="updated"
  fi

  if (( DRY_RUN == 1 )); then
    rm -f "$tmp"
    echo "would $verb $rel"
    return 0
  fi

  mkdir -p "$(dirname "$dest")" || { rm -f "$tmp"; die "mkdir failed for $(dirname "$dest")" 4; }
  if ! mv "$tmp" "$dest"; then
    rm -f "$tmp"
    die "write failed for $dest" 4
  fi
  echo "$verb $rel"
}

# strip_toml_managed <file> <prefix> [<prefix> ...]
#   Print <file>'s contents with all lintguini-managed content removed:
#     1. Any top-level [section] (or [[section]]) whose name equals or
#        begins with "<prefix>." is dropped, header line through to
#        (but not including) the next non-managed section header or EOF.
#     2. Any line matching the lintguini provenance comment pattern
#        ("# Generated by lintguini ...") is dropped — these are added
#        on every write, so a re-band must clear the prior run's marker
#        before the new one is laid down.
#     3. Any line matching the band pattern ("# {Strict,Lenient,Minimal,
#        Band-agnostic} band ...") is dropped for the same reason.
#     4. Leading blank lines and runs of three-or-more consecutive blank
#        lines are collapsed so repeated re-bands don't accrete vertical
#        whitespace.
#
#   Quoted-key sections (`[tool."some.key"]`) are matched by their raw
#   textual form between brackets — not used by lintguini's managed
#   prefixes (`tool.ruff`, `lints`), so the simpler prefix match holds.
strip_toml_managed() {
  local file="$1"; shift
  python3 - "$file" "$@" <<'PYEOF'
import sys, re

with open(sys.argv[1], 'r') as f:
    text = f.read()
prefixes = sys.argv[2:]

# Lintguini's preamble is "provenance line, then any number of comment
# and blank lines, terminated by the first non-comment non-blank line"
# (which is either the managed section header or, in the Cargo.toml
# fragment case, EOF). The band comment wraps for lenient/minimal —
# matching the line-by-line band-name pattern leaks the wrapped half.
# Consuming the whole comment+blank block after the provenance line
# is robust to wrapping. (Cost: writers shouldn't interleave their
# own comments inside the lintguini preamble — those would also be
# consumed. Out-of-preamble user comments are preserved.)
prov_re = re.compile(r'^\s*#\s*Generated by lintguini\b')
section_re = re.compile(r'^\s*\[\[?([A-Za-z0-9_.\-]+)\]?\]')
comment_re = re.compile(r'^\s*#')
blank_re = re.compile(r'^\s*$')

lines = text.splitlines(keepends=True)
out = []
dropping_section = False
i = 0
while i < len(lines):
    line = lines[i]
    if prov_re.match(line):
        # Consume the provenance line plus every subsequent comment-or-
        # blank line; the next loop iteration handles whatever follows.
        i += 1
        while i < len(lines) and (comment_re.match(lines[i]) or blank_re.match(lines[i])):
            i += 1
        continue
    m = section_re.match(line)
    if m:
        name = m.group(1)
        dropping_section = any(name == p or name.startswith(p + '.') for p in prefixes)
    if not dropping_section:
        out.append(line)
    i += 1

result = ''.join(out)
result = re.sub(r'\A\n+', '', result)
result = re.sub(r'\n{3,}', '\n\n', result)
sys.stdout.write(result)
PYEOF
}

# merge_json_top_level <existing-or-empty> <template> <managed_key...>
#   Print a merged JSON object: template's managed keys (in their
#   template order) followed by the existing file's preserved keys
#   (those not in <managed_key...>). jq's `+` operator preserves left
#   operand's iteration order then appends right operand's new keys,
#   so the template's keys ($t) lead the output and `_provenance`
#   stays first.
#
#   Pass /dev/null (or any empty input) for <existing-or-empty> on
#   fresh writes — that path emits the template through jq's same
#   formatter, so fresh-write bytes match merge bytes for byte-equal
#   idempotency on the next run.
merge_json_top_level() {
  local existing="$1" template="$2"; shift 2
  local keys_json
  keys_json="$(printf '%s\n' "$@" | jq -R . | jq -s .)"
  if [[ "$existing" == "/dev/null" || ! -e "$existing" ]]; then
    jq \
      --argjson keys "$keys_json" \
      '. as $t | $t' "$template"
  else
    jq -s \
      --argjson keys "$keys_json" \
      '
        .[0] as $t | .[1] as $e |
        ($e | with_entries(select(.key as $k | $keys | index($k) | not))) as $preserved |
        $t + $preserved
      ' "$template" "$existing"
  fi
}

# -- per-language writers ------------------------------------------------

configure_python() {
  local template="$TEMPLATES/python/$BAND/pyproject.toml"
  local dest="$REPO_ROOT/pyproject.toml"
  local tmp; tmp="$(mktemp)" || die "mktemp failed" 4

  if [[ -e "$dest" ]]; then
    # Merge: template's tool.ruff* sections at the top (with provenance),
    # then the existing file's content with its tool.ruff* sections stripped.
    cat "$template" > "$tmp"
    # Separating blank line if the stripped remainder isn't empty.
    local rest
    rest="$(strip_toml_managed "$dest" "tool.ruff")"
    if [[ -n "$(printf '%s' "$rest" | tr -d '[:space:]')" ]]; then
      printf '\n' >> "$tmp"
      printf '%s' "$rest" >> "$tmp"
    fi
  else
    cp "$template" "$tmp"
  fi

  finalize_write "$tmp" "$dest"
}

configure_javascript() {
  _configure_biome "javascript"
}

configure_typescript() {
  _configure_biome "typescript"
  # tsconfig.json — managed compilerOptions.
  local template="$TEMPLATES/typescript/$BAND/tsconfig.json"
  local dest="$REPO_ROOT/tsconfig.json"
  local tmp; tmp="$(mktemp)" || die "mktemp failed" 4

  if ! merge_json_top_level "$dest" "$template" "_provenance" "_band" "compilerOptions" > "$tmp"; then
    rm -f "$tmp"
    die "tsconfig.json merge failed; existing file may not be valid JSON" 4
  fi

  finalize_write "$tmp" "$dest"
}

# _configure_biome <lang>  — shared write path for biome.json.
_configure_biome() {
  local lang="$1"
  local template="$TEMPLATES/$lang/$BAND/biome.json"
  local dest="$REPO_ROOT/biome.json"
  local tmp; tmp="$(mktemp)" || die "mktemp failed" 4

  if ! merge_json_top_level "$dest" "$template" "_provenance" "_band" "\$schema" "linter" "formatter" > "$tmp"; then
    rm -f "$tmp"
    die "biome.json merge failed; existing file may not be valid JSON" 4
  fi

  finalize_write "$tmp" "$dest"
}

configure_rust() {
  # rustfmt.toml — copy direct from band template.
  local rfm_template="$TEMPLATES/rust/$BAND/rustfmt.toml"
  local rfm_dest="$REPO_ROOT/rustfmt.toml"
  local rfm_tmp; rfm_tmp="$(mktemp)" || die "mktemp failed" 4
  cp "$rfm_template" "$rfm_tmp"
  finalize_write "$rfm_tmp" "$rfm_dest"

  # Cargo.lints.toml fragment — only present for strict/lenient bands.
  # Merged into Cargo.toml's [lints*] sections. Minimal band has no
  # fragment and therefore does not touch Cargo.toml.
  local fragment="$TEMPLATES/rust/$BAND/Cargo.lints.toml"
  if [[ ! -f "$fragment" ]]; then
    return 0
  fi

  local cargo_dest="$REPO_ROOT/Cargo.toml"
  local cargo_tmp; cargo_tmp="$(mktemp)" || die "mktemp failed" 4

  if [[ -e "$cargo_dest" ]]; then
    # Strip existing [lints*] sections, append fragment at the bottom.
    # Cargo.toml convention places [lints] near the end so we preserve
    # the consumer's [package]/[dependencies] ordering at the top.
    local stripped
    stripped="$(strip_toml_managed "$cargo_dest" "lints")"
    printf '%s' "$stripped" > "$cargo_tmp"
    # Ensure exactly one blank line between preserved content and fragment.
    if [[ -n "$(printf '%s' "$stripped" | tr -d '[:space:]')" ]]; then
      # Strip trailing blank lines from preserved content, then add one.
      python3 - "$cargo_tmp" <<'PYEOF'
import sys
p = sys.argv[1]
with open(p, 'r') as f:
    body = f.read()
body = body.rstrip('\n') + '\n\n'
with open(p, 'w') as f:
    f.write(body)
PYEOF
    fi
    cat "$fragment" >> "$cargo_tmp"
  else
    # No Cargo.toml on disk — write only the fragment as a stub.
    # Rust repos always have Cargo.toml in practice, but defend the
    # path so an arbitrary Cargo.lints.toml-only fixture round-trips.
    cp "$fragment" "$cargo_tmp"
  fi

  finalize_write "$cargo_tmp" "$cargo_dest"
}

configure_ruby() {
  local tool="$RUBY_TOOL"
  if [[ -z "$tool" ]]; then
    if [[ -f "$REPO_ROOT/Gemfile" ]] && grep -E '^[[:space:]]*gem[[:space:]]+["'\'']standard["'\'']' "$REPO_ROOT/Gemfile" >/dev/null 2>&1; then
      tool="standardrb"
    else
      tool="rubocop"
    fi
  fi

  if [[ "$tool" == "standardrb" ]]; then
    local template="$TEMPLATES/ruby/standardrb/standard.yml"
    local dest="$REPO_ROOT/standard.yml"
    local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
    cp "$template" "$tmp"
    finalize_write "$tmp" "$dest"
  else
    local template="$TEMPLATES/ruby/rubocop/$BAND/.rubocop.yml"
    local dest="$REPO_ROOT/.rubocop.yml"
    local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
    cp "$template" "$tmp"
    finalize_write "$tmp" "$dest"
  fi
}

configure_go() {
  local template="$TEMPLATES/go/$BAND/.golangci.yml"
  local dest="$REPO_ROOT/.golangci.yml"
  local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
  cp "$template" "$tmp"
  finalize_write "$tmp" "$dest"
}

# -- CI wiring -----------------------------------------------------------

# lint_command_for_language <lang>
#   Print the lint command for <lang> as a shell-quoted line. Used by
#   the CI wiring writers; commands match the rubric's run-locally hints.
lint_command_for_language() {
  case "$1" in
    python)            echo "ruff check ." ;;
    javascript)        echo "npx --yes @biomejs/biome check ." ;;
    typescript)        echo "npx --yes @biomejs/biome check ." ;;
    rust)              echo "cargo clippy --all-targets -- -D warnings" ;;
    ruby)
      if _ruby_uses_standardrb; then
        echo "bundle exec standardrb"
      else
        echo "bundle exec rubocop"
      fi
      ;;
    go)                echo "golangci-lint run" ;;
    *)                 echo "" ;;
  esac
}

_ruby_uses_standardrb() {
  if [[ -n "$RUBY_TOOL" ]]; then
    [[ "$RUBY_TOOL" == "standardrb" ]] && return 0 || return 1
  fi
  if [[ -f "$REPO_ROOT/Gemfile" ]] && grep -E '^[[:space:]]*gem[[:space:]]+["'\'']standard["'\'']' "$REPO_ROOT/Gemfile" >/dev/null 2>&1; then
    return 0
  fi
  return 1
}

# build_github_actions_workflow
#   Emit the lintguini-lint.yml content to stdout. Idempotent: same
#   inputs (TARGET_LANGS, ruby tool selection) -> same bytes.
build_github_actions_workflow() {
  printf '# %s\n' "$PROVENANCE_TEXT"
  printf '# CI lint workflow wired by /lintguini:configure --ci.\n'
  printf 'name: lint\n'
  printf 'on:\n'
  printf '  push:\n'
  printf '    branches: [main]\n'
  printf '  pull_request:\n'
  printf 'jobs:\n'
  printf '  lint:\n'
  printf '    runs-on: ubuntu-latest\n'
  printf '    steps:\n'
  printf '      - uses: actions/checkout@v4\n'
  for l in "${TARGET_LANGS[@]}"; do
    local cmd; cmd="$(lint_command_for_language "$l")"
    [[ -z "$cmd" ]] && continue
    printf '      - name: lint (%s)\n' "$l"
    printf '        run: %s\n' "$cmd"
  done
}

# build_makefile_target
#   Emit a Makefile fragment with a `lintguini-lint:` target.
build_makefile_target() {
  printf '\n# %s\n' "$PROVENANCE_TEXT"
  printf '# Lint target wired by /lintguini:configure --ci.\n'
  printf '.PHONY: lintguini-lint\n'
  printf 'lintguini-lint:\n'
  for l in "${TARGET_LANGS[@]}"; do
    local cmd; cmd="$(lint_command_for_language "$l")"
    [[ -z "$cmd" ]] && continue
    printf '\t%s\n' "$cmd"
  done
}

wire_ci() {
  # GitHub Actions takes precedence when present.
  if [[ -d "$REPO_ROOT/.github/workflows" ]]; then
    local workflow="$REPO_ROOT/.github/workflows/lintguini-lint.yml"
    local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
    build_github_actions_workflow > "$tmp"
    finalize_write "$tmp" "$workflow"
    return 0
  fi

  if [[ -f "$REPO_ROOT/.gitlab-ci.yml" ]]; then
    # Idempotency: only append a job once. Detect by provenance line.
    local dest="$REPO_ROOT/.gitlab-ci.yml"
    local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
    cp "$dest" "$tmp"
    if grep -F -q "$PROVENANCE_TEXT" "$tmp"; then
      # Already wired — let finalize_write report skipped.
      :
    else
      printf '\n# %s\n' "$PROVENANCE_TEXT" >> "$tmp"
      printf '# Lint stage wired by /lintguini:configure --ci.\n' >> "$tmp"
      printf 'lintguini-lint:\n' >> "$tmp"
      printf '  stage: test\n' >> "$tmp"
      printf '  script:\n' >> "$tmp"
      for l in "${TARGET_LANGS[@]}"; do
        local cmd; cmd="$(lint_command_for_language "$l")"
        [[ -z "$cmd" ]] && continue
        printf '    - %s\n' "$cmd" >> "$tmp"
      done
    fi
    finalize_write "$tmp" "$dest"
    return 0
  fi

  if [[ -f "$REPO_ROOT/Makefile" ]]; then
    local dest="$REPO_ROOT/Makefile"
    local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
    cp "$dest" "$tmp"
    if grep -F -q "$PROVENANCE_TEXT" "$tmp"; then
      :
    else
      build_makefile_target >> "$tmp"
    fi
    finalize_write "$tmp" "$dest"
    return 0
  fi

  if [[ -f "$REPO_ROOT/lefthook.yml" ]]; then
    local dest="$REPO_ROOT/lefthook.yml"
    local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
    cp "$dest" "$tmp"
    if grep -F -q "$PROVENANCE_TEXT" "$tmp"; then
      :
    else
      printf '\n# %s\n' "$PROVENANCE_TEXT" >> "$tmp"
      printf '# Lint hook wired by /lintguini:configure --ci.\n' >> "$tmp"
      printf 'pre-commit:\n' >> "$tmp"
      printf '  commands:\n' >> "$tmp"
      printf '    lintguini-lint:\n' >> "$tmp"
      printf '      run: |\n' >> "$tmp"
      for l in "${TARGET_LANGS[@]}"; do
        local cmd; cmd="$(lint_command_for_language "$l")"
        [[ -z "$cmd" ]] && continue
        printf '        %s\n' "$cmd" >> "$tmp"
      done
    fi
    finalize_write "$tmp" "$dest"
    return 0
  fi

  if [[ -f "$REPO_ROOT/.pre-commit-config.yaml" ]]; then
    local dest="$REPO_ROOT/.pre-commit-config.yaml"
    local tmp; tmp="$(mktemp)" || die "mktemp failed" 4
    cp "$dest" "$tmp"
    if grep -F -q "$PROVENANCE_TEXT" "$tmp"; then
      :
    else
      printf '\n# %s\n' "$PROVENANCE_TEXT" >> "$tmp"
      printf '# Lint hook wired by /lintguini:configure --ci.\n' >> "$tmp"
      printf '  - repo: local\n' >> "$tmp"
      printf '    hooks:\n' >> "$tmp"
      printf '      - id: lintguini-lint\n' >> "$tmp"
      printf '        name: lintguini lint\n' >> "$tmp"
      printf '        entry: bash -c "%s"\n' "$(printf '%s' "${TARGET_LANGS[*]}" | tr ' ' '\n' | while read -r l; do lint_command_for_language "$l"; done | paste -sd ' && ' -)" >> "$tmp"
      printf '        language: system\n' >> "$tmp"
      printf '        pass_filenames: false\n' >> "$tmp"
    fi
    finalize_write "$tmp" "$dest"
    return 0
  fi

  warn "no CI surface detected; --ci skipped (looked for .github/workflows/, .gitlab-ci.yml, Makefile, lefthook.yml, .pre-commit-config.yaml)"
}

# -- main loop -----------------------------------------------------------

for lang in "${TARGET_LANGS[@]}"; do
  case "$lang" in
    python)     configure_python ;;
    javascript) configure_javascript ;;
    typescript) configure_typescript ;;
    rust)       configure_rust ;;
    ruby)       configure_ruby ;;
    go)         configure_go ;;
    *)          warn "unknown language '$lang' from detector — skipping" ;;
  esac
done

if (( CI == 1 )); then
  wire_ci
fi

exit 0
