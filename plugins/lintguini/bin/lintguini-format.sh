#!/usr/bin/env bash
# lintguini-format.sh — run the lintguini-configured formatter(s)
# against a target consumer repo. Two modes:
#
#   default (apply)  — mutate files in place; print one line per
#                      modified file: `formatted <path>`. Exit 0 on
#                      success.
#   --check          — diff-only / read-only. Surface what would
#                      change. Exit non-zero (1) when any file would
#                      change. CI-friendly.
#
# Determinism contract: this is the deterministic half of
# /lintguini:format. The skill body (skills/format/SKILL.md) is the
# LLM-shaped half. Same REPO_ROOT state + same flags + same tool
# versions on PATH -> byte-equivalent stdout across runs (modulo the
# tool's own diff output in --check mode, which is the tool's contract,
# not ours).
#
# ADR-006: in apply mode this skill mutates the consumer's working
# tree — that's its purpose. The mutation is bounded to source files
# the formatter recognises; ADR-006 §2's "no silent mutation of
# consumer artefacts" allowance for capability surfaces (consumer
# invoked the skill explicitly) applies.
#
# ADR-008: rubric pins the canonical formatter per language. We
# dispatch exactly that; no fallback to a "second-best" formatter.
#
# Per-language tool dispatch:
#
#   Language    Apply                       Check
#   --------    -----                       -----
#   python      ruff format .               ruff format --check .
#   javascript  biome format --write .      biome check --formatter-enabled=true
#                                             --linter-enabled=false --reporter=github .
#   typescript  biome format --write .      same as javascript
#   rust        cargo fmt                   cargo fmt -- --check
#   ruby (rubocop)    rubocop -A .          rubocop --format json . (read-only)
#   ruby (standardrb) standardrb --fix .    standardrb --format json . (read-only)
#   go          gofmt -w .                  gofmt -l .   (file list)
#                                           gofmt -d .   (diff to stdout)
#
# Apply-mode strategy: the apply command rewrites files; we run a
# check pass first to capture the file list (per-tool parser; see
# files_<tool>_check below). The check pass identifies which files
# would change; the apply pass actually changes them; the file list
# from the pre-check pass becomes the `formatted <path>` lines on
# stdout. Two tool invocations per language is the cost of providing
# a tool-agnostic per-file output contract.
#
# Usage:
#   lintguini-format.sh [--language <lang>] [--check] <REPO_ROOT>
#
# Flags:
#   --language <lang>  Scope to a single language (one of:
#                      python, javascript, typescript, rust, ruby, go).
#                      Without it, format every detected + configured
#                      language.
#   --check            Diff-only mode. Surface what would change to
#                      stdout, exit 1 if any file would change.
#                      Default: apply changes in place.
#   -h | --help        Show this help.
#
# Polyglot output:
#   When two or more configured languages are formatted in a single
#   run, stdout is sectioned with a metadata header line:
#
#     # python — 3 files formatted        (apply mode)
#     # python — 3 files would be reformatted   (--check mode)
#
#   The header line begins with `# `. Single-language runs emit no
#   header.
#
# Empty-scope and tool-missing diagnostics go to stderr, never stdout:
#
#   <language>: not configured (run /lintguini:configure --language <lang>)
#   <language>: <tool> not on PATH (install <tool>)
#
# Exit codes:
#   0  apply mode succeeded; or --check found nothing to change
#   1  --check found at least one file that would change
#   2  argument errors
#   3  required tooling missing on PATH for an in-scope language
#   4  formatter execution failure (apply tool crashed or returned
#      an unrecognised exit code)

set -uo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_ROOT="$(cd "$HERE/.." && pwd)"
DETECT_BIN="$PLUGIN_ROOT/bin/lintguini-detect-language.sh"

PROVENANCE_MARK='Generated by lintguini'

usage() {
  sed -n '/^# Usage:/,/^# Exit codes:/p' "$0" | sed 's/^# \{0,1\}//' >&2
}

die() {
  local msg="$1" rc="${2:-2}"
  echo "lintguini-format: $msg" >&2
  exit "$rc"
}

# -- argument parsing ----------------------------------------------------

LANGUAGE=""
CHECK_MODE=0
REPO_ROOT_ARG=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --language)
      [[ $# -ge 2 ]] || die "--language requires a value"
      LANGUAGE="$2"; shift 2 ;;
    --language=*)
      LANGUAGE="${1#--language=}"; shift ;;
    --check)
      CHECK_MODE=1; shift ;;
    -h|--help)
      usage; exit 0 ;;
    --)
      shift
      while [[ $# -gt 0 ]]; do
        [[ -n "$REPO_ROOT_ARG" ]] && die "unexpected positional '$1'"
        REPO_ROOT_ARG="$1"; shift
      done ;;
    --*)
      die "unknown flag '$1'" ;;
    *)
      [[ -n "$REPO_ROOT_ARG" ]] && die "unexpected positional '$1'"
      REPO_ROOT_ARG="$1"; shift ;;
  esac
done

if [[ -n "$LANGUAGE" ]]; then
  case "$LANGUAGE" in
    python|javascript|typescript|rust|ruby|go) ;;
    *) die "--language must be one of: python javascript typescript rust ruby go (got '$LANGUAGE')" ;;
  esac
fi

[[ -z "$REPO_ROOT_ARG" ]] && { usage; die "REPO_ROOT positional argument required"; }
[[ -d "$REPO_ROOT_ARG" ]] || die "REPO_ROOT '$REPO_ROOT_ARG' is not a directory"
REPO_ROOT="$(cd "$REPO_ROOT_ARG" && pwd)"

# -- host tooling check --------------------------------------------------

command -v jq >/dev/null 2>&1 || die "jq is required" 3

# -- detection -----------------------------------------------------------

mapfile -t DETECTED < <("$DETECT_BIN" "$REPO_ROOT")

if [[ -n "$LANGUAGE" ]]; then
  found=0
  for l in "${DETECTED[@]}"; do
    [[ "$l" == "$LANGUAGE" ]] && { found=1; break; }
  done
  (( found == 1 )) || die "--language $LANGUAGE not detected in $REPO_ROOT (detected: ${DETECTED[*]:-none})"
  TARGET_LANGS=("$LANGUAGE")
else
  if [[ ${#DETECTED[@]} -eq 0 ]]; then
    echo "lintguini: no supported languages detected (need pyproject.toml/Cargo.toml/go.mod/Gemfile/.rubocop.yml/standard.yml/.standard.yml/tsconfig.json/package.json)" >&2
    exit 0
  fi
  TARGET_LANGS=("${DETECTED[@]}")
fi

# -- config-presence detection (mirrors lintguini-lint.sh) ---------------

is_configured() {
  local lang="$1"
  case "$lang" in
    python)
      [[ -f "$REPO_ROOT/pyproject.toml" ]] \
        && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/pyproject.toml"
      ;;
    javascript|typescript)
      [[ -f "$REPO_ROOT/biome.json" ]] \
        && jq -e --arg m "$PROVENANCE_MARK" \
            '(._provenance // "") | contains($m)' "$REPO_ROOT/biome.json" >/dev/null 2>&1
      ;;
    rust)
      [[ -f "$REPO_ROOT/rustfmt.toml" ]] \
        && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/rustfmt.toml"
      ;;
    ruby)
      ( [[ -f "$REPO_ROOT/standard.yml" ]] && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/standard.yml" ) \
        || ( [[ -f "$REPO_ROOT/.rubocop.yml" ]] && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/.rubocop.yml" )
      ;;
    go)
      [[ -f "$REPO_ROOT/.golangci.yml" ]] \
        && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/.golangci.yml"
      ;;
    *)
      return 1
      ;;
  esac
}

ruby_tool() {
  if [[ -f "$REPO_ROOT/standard.yml" ]] && grep -F -q "$PROVENANCE_MARK" "$REPO_ROOT/standard.yml"; then
    echo "standardrb"
  else
    echo "rubocop"
  fi
}

# -- per-tool check parsers ----------------------------------------------
#
# Each function prints one would-change file path per line on stdout,
# sorted. They run the tool in its native check mode and parse the
# tool's output. Empty stdout means nothing to change.
#
# These parsers are small by design — if a tool's check output format
# shifts in a future release, we update one function. Mock-tool tests
# at scorers/tests/format.test.sh exercise the parser shape.

tool_missing() {
  echo "$1: $2 not on PATH (install $2)" >&2
  exit 3
}

tool_failure() {
  echo "$1: $2 execution failed — $3" >&2
  exit 4
}

sort_lines() { LC_ALL=C sort -u; }

files_python() {
  command -v ruff >/dev/null 2>&1 || tool_missing python ruff
  ( cd "$REPO_ROOT" && ruff format --check . 2>/dev/null ) \
    | sed -n 's/^Would reformat: //p' | sort_lines
}

files_biome() {
  local lang="$1"
  command -v biome >/dev/null 2>&1 || tool_missing "$lang" biome
  ( cd "$REPO_ROOT" && biome check --formatter-enabled=true --linter-enabled=false --reporter=github . 2>/dev/null ) \
    | sed -n 's|^::[a-zA-Z]\+ .*file=\([^,]*\).*|\1|p' | sort_lines
}

files_rust() {
  command -v cargo >/dev/null 2>&1 || tool_missing rust cargo
  ( cd "$REPO_ROOT" && cargo fmt -- --check 2>/dev/null ) \
    | sed -n 's/^Diff in \(.*\) at line.*/\1/p' | sort_lines
}

files_ruby() {
  local tool; tool="$(ruby_tool)"
  command -v "$tool" >/dev/null 2>&1 || tool_missing ruby "$tool"
  local out
  out="$(cd "$REPO_ROOT" && "$tool" --format json . 2>/dev/null)"
  [[ -z "$out" ]] && return 0
  echo "$out" | jq -r '
    .files[]?
    | select(((.offenses // []) | length) > 0)
    | .path
  ' 2>/dev/null | sort_lines
}

files_go() {
  command -v gofmt >/dev/null 2>&1 || tool_missing go gofmt
  ( cd "$REPO_ROOT" && gofmt -l . 2>/dev/null ) | sort_lines
}

# -- per-language apply commands -----------------------------------------

apply_python()     { ( cd "$REPO_ROOT" && ruff format . >/dev/null 2>&1 ); }
apply_javascript() { ( cd "$REPO_ROOT" && biome format --write . >/dev/null 2>&1 ); }
apply_typescript() { ( cd "$REPO_ROOT" && biome format --write . >/dev/null 2>&1 ); }
apply_rust()       { ( cd "$REPO_ROOT" && cargo fmt >/dev/null 2>&1 ); }
apply_ruby() {
  local tool; tool="$(ruby_tool)"
  if [[ "$tool" == "standardrb" ]]; then
    ( cd "$REPO_ROOT" && standardrb --fix . >/dev/null 2>&1 )
  else
    ( cd "$REPO_ROOT" && rubocop -A . >/dev/null 2>&1 )
  fi
}
apply_go()         { ( cd "$REPO_ROOT" && gofmt -w . >/dev/null 2>&1 ); }

# -- per-language driver -------------------------------------------------
#
# Driver strategy:
#   --check mode:
#     1. Run the per-tool check parser to enumerate files that would
#        change.
#     2. Print `would format <path>` per file on stdout (sorted).
#     3. Caller computes section count from the line count and exits
#        1 globally if any file would change.
#   apply mode:
#     1. Run the same per-tool check parser to enumerate files that
#        WILL change once the apply runs.
#     2. Run the per-language apply command.
#     3. Print `formatted <path>` per file on stdout (sorted).
#     The pre-apply file list IS the apply-mode contract — if the apply
#     command itself fails, we exit 4.

format_lang() {
  local lang="$1"
  local files
  case "$lang" in
    python)     files="$(files_python)" ;;
    javascript) files="$(files_biome javascript)" ;;
    typescript) files="$(files_biome typescript)" ;;
    rust)       files="$(files_rust)" ;;
    ruby)       files="$(files_ruby)" ;;
    go)         files="$(files_go)" ;;
  esac

  if (( CHECK_MODE == 1 )); then
    [[ -z "$files" ]] && return 0
    while IFS= read -r f; do
      [[ -z "$f" ]] && continue
      printf 'would format %s\n' "$f"
    done <<< "$files"
    return 0
  fi

  # apply mode
  case "$lang" in
    python)     apply_python     || tool_failure "$lang" ruff "format apply failed" ;;
    javascript) apply_javascript || tool_failure "$lang" biome "format apply failed" ;;
    typescript) apply_typescript || tool_failure "$lang" biome "format apply failed" ;;
    rust)       apply_rust       || tool_failure "$lang" "cargo fmt" "format apply failed" ;;
    ruby)       apply_ruby       || tool_failure "$lang" "$(ruby_tool)" "format apply failed" ;;
    go)         apply_go         || tool_failure "$lang" gofmt "format apply failed" ;;
  esac

  [[ -z "$files" ]] && return 0
  while IFS= read -r f; do
    [[ -z "$f" ]] && continue
    printf 'formatted %s\n' "$f"
  done <<< "$files"
}

# -- main loop -----------------------------------------------------------

CONFIGURED_LANGS=()
for lang in "${TARGET_LANGS[@]}"; do
  if is_configured "$lang"; then
    CONFIGURED_LANGS+=("$lang")
  else
    echo "$lang: not configured (run /lintguini:configure --language $lang)" >&2
  fi
done

if [[ ${#CONFIGURED_LANGS[@]} -eq 0 ]]; then
  exit 0
fi

EMIT_HEADERS=0
(( ${#CONFIGURED_LANGS[@]} > 1 )) && EMIT_HEADERS=1

# Section header verb varies per mode.
#   apply mode:  "<N> file[s] formatted"
#   check mode:  "<N> file[s] would be reformatted"
header_verb_singular() {
  if (( CHECK_MODE == 1 )); then echo "would be reformatted"; else echo "formatted"; fi
}

TOTAL_CHANGED=0
for lang in "${CONFIGURED_LANGS[@]}"; do
  out="$(format_lang "$lang")"
  format_rc=$?
  if (( format_rc == 3 || format_rc == 4 )); then
    exit "$format_rc"
  fi
  if [[ -z "$out" ]]; then
    count=0
  else
    count=$(printf '%s\n' "$out" | grep -c '.' || true)
  fi
  if (( EMIT_HEADERS == 1 )); then
    if (( CHECK_MODE == 1 )); then
      if (( count == 1 )); then
        printf '# %s — 1 file would be reformatted\n' "$lang"
      else
        printf '# %s — %s files would be reformatted\n' "$lang" "$count"
      fi
    else
      if (( count == 1 )); then
        printf '# %s — 1 file formatted\n' "$lang"
      else
        printf '# %s — %s files formatted\n' "$lang" "$count"
      fi
    fi
  fi
  if [[ -n "$out" ]]; then
    printf '%s\n' "$out"
  fi
  TOTAL_CHANGED=$((TOTAL_CHANGED + count))
done

if (( CHECK_MODE == 1 && TOTAL_CHANGED > 0 )); then
  exit 1
fi
exit 0
